<p><strong>BOB &amp; BARBARA KLEIST ENDOWED STUDENT AWARD IN ICS</strong></p>
<p><strong>OVERVIEW:</strong> The Bob and Barbara Kleist Endowment was established through the generous donation from Bob and Barbara Kleist themselves. This award is designated to support undergraduate students who have transferred to UCI from a community college and who are preparing for a career in the computer science field. Students must have a declared major in the Donald Bren School of Information and Computer Sciences.</p>
<p><strong>AWARDS:</strong> 2 awards of $2,500 each</p>
<p><strong>SELECTION CRITERIA:</strong></p>
<ul>
<li>Transfer students only</li>
<li>Academic excellence</li>
<li>Essay required</li>
</ul>
<hr size="0"/>
<p><strong>STEVE &amp; JENNY MIZUSAWA ENDOWED STUDENT AWARD IN ICS</strong></p>
<p><strong>OVERVIEW:</strong> The Steve and Jenny Mizusawa Student Award Endowment was established in 2005 through the generous donation from Steve and Jenny themselves. This award is designated to support undergraduate juniors and seniors preparing for a career in the computer science field. Students must have a declared major in the Donald Bren School of Information and Computer Sciences.</p>
<p><strong>AWARD:</strong> 1 award of $1,500</p>
<p><strong>SELECTION CRITERIA:</strong></p>
<ul>
<li>Juniors and Seniors</li>
<li>Minimum ICS GPA of 3.0</li>
<li>Self nomination and/or faculty recommendation</li>
<li>Essay required</li>
</ul>

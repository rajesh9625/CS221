<p><strong>ACCENTURE ENDOWED OUTSTANDING JUNIOR AWARD IN ICS</strong></p>
<p><strong>OVERVIEW:</strong>&#160;<span>The Accenture Endowed Outstanding Junior Award was established through and endowment in 1992 by Accenture to recognize and financially assist selected student recipients during their final year at UC Irvine.</span> <br/> <br/> <strong>AWARD:</strong> 1 award of $1,500</p>
<p><strong>SELECTION CRITERIA:</strong></p>
<ul>
<li>3.0 ICS GPA minimum</li>
<li>Graduate in the following academic year</li>
<li>40% academic standing</li>
<li>30% demonstrated leadership abilities</li>
<li>30% civic and/or charitable involvement (eg. volunteerism with various charity or civic minded organizations or individuals efforts taken by the applicants)</li>
<li>Essay required</li>
</ul>
<hr/>
<p><strong>ESSIE LEV ENDOWED MEMORIAL STUDENT AWARD IN ICS<br/> <br/> OVERVIEW: </strong>The Essie Lev Endowed Memorial Student Award was established by Sara Sandel to honor her sister and former UC Irvine academic counselor Essie Lev. The award is designated for transfer or re-entering undergraduate students with demonstrated financial need. Students must have a declared major in the Donald Bren School of Information and Computer Sciences.</p>
<p><strong>AWARDS:</strong> 2 awards of $1,250 each</p>
<p><strong>SELECTION CRITERIA:</strong></p>
<ul>
<li>Transfer or re-entry students only</li>
<li>ICS majors</li>
<li>Demonstrated campus or community service</li>
<li>Essay required</li>
</ul>
<hr/>
<p><strong>JULIAN FELDMAN ENDOWED SCHOLARSHIP IN ICS<br/> <br/> OVERVIEW: </strong>The Julian Feldman Scholarship was established in 1998 by Jim Hobbs &#8217;73 in honor of ICS Professor Emeritus Julian Feldman. The scholarship used to be funded through an annual gift made by Jim and Trinidad Hobbs. In 2006, the scholarship was turned into an Endowed Scholarship, now called, The Julian Feldman Endowed Scholarship in ICS, funded through Jim Hobbs&#8217; annual gifts as well as gifts from ICS&#8217; Annual Fund campaign.</p>
<p><strong>AWARDS:</strong> 2 awards of $1,250 each</p>
<p><strong>SELECTION CRITERIA:</strong></p>
<ul>
<li>High academic standing</li>
<li>Possess demonstrated leadership abilities</li>
<li>No essay required</li>
</ul>
<hr/>
<p><strong>KENNETH SIMMS ENDOWED MEMORIAL SCHOLARSHIP IN ICS</strong></p>
<p><strong>OVERVIEW:</strong> The Kenneth Simms Memorial Scholarship endowment was established in 1989 by Laguna Software to honor UCI alumnus and one of the most significant contributors to the development of the PICK Operating System, Kenneth Simms &#8217;70.</p>
<p><strong>AWARDS:</strong> 2 awards of $2,000 each</p>
<p><strong>SELECTION CRITERIA:</strong></p>
<ul>
<li>Academic excellence<b><br/></b></li>
<li>Recipient must be a U.S. citizen or a permanent resident</li>
<li>The recipient should be preparing for a profession in the field of computer science</li>
<li>The recipient is selected by the selection committe according to the guidelines</li>
<li>Demonstrated financial need</li>
<li>No essay required</li>
</ul>
<hr/>
<p><strong>SUMALEE JOHNSON TRANSFER STUDENT AWARD IN ICS</strong></p>
<p><strong>OVERVIEW:</strong> The Sumalee Johnson Transfer Student Award was established through the generous donation from ICS alumnus, Sumlalee Johnson '82. This award is designated to support undergraduate students who have transferred to UCI from a community college and who are preparing for a career in the computer science field. Students must have a declared major in the Donald Bren School of Information and Computer Sciences.</p>
<p><strong>AWARD:</strong> 1 award of $2,000</p>
<p><strong>SELECTION CRITERIA:</strong></p>
<ul>
<li>Transfer students only</li>
<li>3.2 ICS GPA minimum</li>

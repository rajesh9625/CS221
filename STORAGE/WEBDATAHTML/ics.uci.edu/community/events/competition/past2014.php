<p><b>WINNERS</b></p>
<div class="section">
<div class="section_heading">Beall Student Design Competition at the Samueli School</div>
<div style="margin-left: 1em;">
<ul>
<li>1st Place Prize Winners ($7,500):
<div class="team"><b>Spero Diagnostics</b>
<p><b>Project Description:</b> This product addresses the dangerous, even deadly prevalence of sepsis in hospital settings. Today, a full sepsis diagnostic process takes up to four days, requires multiple tests, advanced trained personnel, and costs several hundred dollars. Spero Diagnostics&#8217; goal was to reduce that time frame to less than three hours and bring the cost down to under $100. Their design is a software package that automates a device that performs complicated diagnostic tests and reduces the volumes of reagents and samples. The design also includes pre-filled blister packs that both store and dispense all the appropriate reagents and volumes needed to perform tests. The automated device and blister packs will allow a minimally trained user to perform complicated sepsis diagnostic tests at the point of care.</p>
<p><b>Students:</b> Jessica Motherwell, Jose Gallegos, Ernesto Sosa, Ting-Yi Chu, Maka Pennell, Zachary Campagna</p>
</div>
</li>
<li>2nd Place Prize Winners ($5,000):
<div class="team"><b>A Hundred Tiny Hands</b>
<p><b>Project Description:</b> A Hundred Tiny Hands developed three &#8220;Inventor&#8217;s Toolboxes&#8221; designed to teach children core concepts in engineering and science in a fun and interactive way. The products take the latest discoveries in science and use current engineering methodologies to create an educational toolbox, tailored to children of all ages. The Inventor&#8217;s Toolboxes encourage children to explore, create, and invent by allowing them to build their own structures and models. The simple design of these three Inventor&#8217;s Toolboxes makes even the most cutting edge scientific technologies easy enough for any child or parent to learn. The team&#8217;s goal and vision is to create and foster an online community of young inventors able to interact and share with one another, similar to scientists in real life.</p>
<p><b>Students:</b> Sophia Lin, Nicole Mendoza, Jolie McLane, Eugene Lee, Danny Greene</p>
</div>
</li>
<li>3rd Place Prize Winners ($2,500):
<div class="team"><b>HydroTrack</b>
<p><b>Project Description:</b> Capitalizing on the rapidly growing digital fitness market and the rapidly falling costs of electronics, HydroTrack is a smart hydration tool that helps make keeping track of one&#8217;s hydration easier than it has ever been. Using Bluetooth low-energy and a novel flow sensor, HydroTrack transmits how much water one is consuming to one&#8217;s smartphone or favorite digital fitness device. The accompanying smartphone application sends gentle reminders when hydration goals are not met, and coaches the user to drink the appropriate amount of water based on activity level.</p>
<p><b>Students:</b> Michael Bryant, Aurelia Darling, Anni Liu</p>
</div>
</li>
</ul>
</div>
</div>
<!-- --><!-- --><!-- -->
<div class="section">
<div class="section_heading">Butterworth Product Development Competition at the Donald Bren School of Information and Computer Sciences</div>
<div style="margin-left: 1em;">
<ul>
<li>1st Place Prize Winners ($7,500):
<div class="team"><b>Blueberry</b>
<p><b>Project Description:</b> The app its creators now call &#8220;Sparky&#8221; is a networking tool. Turning on the app on a smartphone creates a 10-foot Bluetooth bubble around the person. Whenever one &#8220;Sparky&#8221; bubble encounters another, a silent connection is created. Throughout the day, the app monitors interactions collecting metadata like time, location, and duration. The profile pages and interaction metadata the app offers are so comprehensive that the need to take notes is all but eliminated. Once professionals are ready to follow up, &#8220;Sparky&#8221; presents them with an intelligently sorted list of everybody they interacted with at an event.</p>
<p><b>Students:</b> Karan Sekhri, Adrien Deguzman, Sheng Xia, Tai Cao, Derek Omuro</p>
</div>
</li>
<li>2nd Place Prize Winners ($5,000):
<div class="team"><b>Sprout</b>
<p><b>Project Description:</b> &#8220;BeatPool&#8221; allows multiple music-player users to send songs from their music libraries to one speaker &#8211; creating a kind of crowdsourced playlist. Every device that has a &#8220;BeatPool&#8221; app on it can send songs from its own library into collaborative pool. The system is structured in a way that allows it to function either in crowded environments such as parties, or even on camping trips.</p>
<p><b>Students:</b> Ekin Oguz, Sky Faber, Cesar Ghali, Fulya Ozcan</p>
</div>
</li>
<li>3rd Place Prize Winners ($2,500):
<div class="team"><b>Ant mApp</b>
<p><b>Project Description:</b> &#8220;Ant-mApp&#8221; is a mobile and website application that creates a central hub of event information for on-campus events at UCI. It serves as a platform where advertisers of events, such as student organizations and school departments, can meet the seekers of event information, the audience. By allowing users to filter through events and subscribe to groups and categories of interest, &#8220;Ant-mApp&#8221; will enhance the community involvement and learning experience of UCI students. &#8220;Ant-mApp&#8221; is a sustainable alternative to flyering intended to grow and strengthen the bonds of members of the UCI community.</p>
<p><b>Students:</b> Alexsandra Guerra, Esosa Agbonwaneten, Yerlan Turekeshov, Avedis Simonian, Maharshi Patel</p>
</div>
</li>
</ul>
</div>

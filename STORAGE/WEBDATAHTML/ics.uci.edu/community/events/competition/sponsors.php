<p>
<b>ABOUT THE SPONSORS</b>
</p>
<div style="margin: 2em 0;">
<p>
<b>Butterworth Product Development Competition at the Donald Bren School of Information and Computer Sciences</b>
</p>
<p>
<img alt="Paul Butterworth" src="butterworth.jpg" style="width: 70px; float: left; margin: 0 1em 1em 0;" width="140px"/>
<b>Paul Butterworth '74, M.S. '81</b> <br/>
Chief Technology Officer, Appconomy Inc.
</p>
<p>
Paul is currently CTO at Appconomy Inc. Past roles include Co-founder and CTO of Emotive, where he conceived and designed the Emotive Cloud Platform for enterprise mobile computing. Before that, Paul was an Architect at Oracle and a founder &amp; CTO at AmberPoint where directed the technical strategy for the AmberPoint SOA governance products. Prior to AmberPoint, Paul was a Distinguished Engineer and Chief Technologist for the Developer Tools Group at Sun Microsystems and a founder, Chief Architect and Senior Vice President of Forte Software. Paul also served as Chief Systems Architect at Ingres. Paul holds undergraduate and graduate degrees in Computer Science from UC Irvine.
</p>
</div>
<div style="margin: 2em 0;">
<p>
<b>Beall Student Design Competition at The Henry Samueli School of Engineering</b>
</p>
<p>
<img alt="Donald Beall" src="beall.jpg" style="width: 70px; float: left; margin: 0 1em 1em 0;" width="140px"/>
<b>Donald R. Beall</b> <br/>
Chairman Emeritus <br/>
Retired Chairman &amp; Chief Executive Officer, Rockwell <br/>
Partner, Dartbrook Partners, LLC (a family partnership)
</p>
<p>
Don retired as Chairman/CEO in 1998 after a 30-year career with Rockwell. When he stepped down, he was named Chairman Emeritus and continues to serve as a director on the board of Rockwell Collins. He also currently serves on the board of CT Realty and is a former director of Conexant Systems, Mindspeed Technologies, Skyworks Solutions, Proctor &amp; Gamble, Amoco, Rockwell, Times Mirror and ArvinMeritor. He is the founding partner in Dartbrook Partners (a family partnership) and the chairman of the Beall Family Foundation.
</p>
<p>
Don serves on several advisory boards and executive committees. At UC Irvine, his involvement includes: the UCI Paul Merage School of Business Dean&#8217;s Advisory Board, Executive Committee, and the Don Beall Center for Innovation and Entrepreneurship Advisory Board; UCI Henry Samueli School of Engineering Advisory Board; UCI Beall Center for Art + Technology Advisory Board; UCI Medical Center CEO Advisory Board and the UCI Chief Executive Roundtable. He is an advisor to the San Jose State University School of Engineering and member of the Engineering Leadership Council; and a trustee and President&#8217;s Circle member of the Naval Postgraduate School Foundation. He served as an Overseer of the Hoover Institution at Stanford from 1997 until 2011, and remains active on the Hoover Council today. He is involved in numerous professional, educational, public service and philanthropic endeavors and is an investor, director, and/or advisor with several venture capital groups, individual companies and investment partnerships.
</p>
<p>
Don earned an engineering degree from San Jose State University and an MBA from the University of Pittsburgh. He has been married for more than 50 years to Joan Beall. They have two married sons, two granddaughters and three grandsons. Don and Joan reside in Corona del Mar and Pebble Beach, California.</p>
</div>



<p><b>WINNERS</b></p>
<div class="section">
<div class="section_heading">Beall Student Design Competition at the Samueli School</div>
<div style="margin-left: 1em;">
<ul>
<li>1st Place ($7,500):
<div class="team"><span class="team_name">MuTech Scientific</span>
<p><b>Project Description:</b> DECAY-Z is a device that will help promote the ease of access to HIV diagnostics by decreasing the cost of production through microfluidics. Microfluidics allow greater reagent efficiencies with increased reaction surface area to fluid ratios. This enables a reduced cost by minimizing the most expensive regents in current diagnostics platforms.</p>
<p><b>Students:</b> Ya Kevin Thao, Abraham Phung, Diego Sandoval, Eric Wilde, Zaw Mai, Cory Schoenborn, Michael Bulotano</p>
</div>
</li>
<!-- -->
<li>2nd Place ($5,000):
<div class="team"><span class="team_name">Sixth Sense</span>
<p><b>Project Description:</b> Energy Conservation Observer (ECO) is a context-aware device that accumulates a user&#8217;s energy consumption data, senses personal energy waste and provides eco-feedback to promote sustainability in the following ways: It will understand the user&#8217;s energy needs and learn her usage habits to alert her (and potentially the public utility company) when energy wastage is detected. It will prompt the user to take action by correlating utility bills with ECO&#8217;s recommendations. It will illuminate the context in which the user can contribute to save resources by giving timely alerts.</p>
<p><b>Students:</b> Shriti Raj, Neeraj Kumar, LouAnne Boyd, Ali Shahbaz</p>
</div>
</li>
<!-- -->
<li>3rd Place ($2,500):
<div class="team"><span class="team_name">OneSeed</span>
<p><b>Project Description:</b> Similar to nurturing a child&#8217;s development, experiencing the plant lifecycle from seed, to seedling, to mature plant, provides people with intrinsic joy and happiness. Rather than an enjoyable hobby, gardening is perceived by most as intimidating and difficult. OneSeed will change this perception by simplifying and demystifying gardening. OneSeed is a &#8220;smart&#8221; gardening &#8220;system&#8221; that will remove the guesswork from gardening, making it easy and even FUN! By integrating multiple sensors and wireless technologies into a portable OneSeed Smart Unit, we&#8217;re able to collect and monitor vital parameters (e.g. soil moisture; UV exposure; ambient temperature; pH) to determine the condition and exact needs of plants without consumer intervention or the need for specific gardening knowledge/experience.</p>
<p><b>Students:</b> Stella Liu, Vouy Yeng, Sandy Pham, Aaron Rosario, Briana O'Hern, Erik Henriquez, Ryan Kuhs</p>
</div>
</li>
</ul>
</div>
</div>
<!-- --><!-- --><!-- -->
<div class="section">
<div class="section_heading">Butterworth Product Development Competition at the Donald Bren School of Information and Computer Sciences</div>
<div style="margin-left: 1em;">
<ul>
<li>1st Place ($7,500):
<div class="team"><span class="team_name">Roco</span>
<p><b>Project Description:</b> Roco is an intelligent mobile application that aims to use machine learning methods to create an automated private photosharing system. Using the application, users can capture and share pictures in an effortless way. A distinctive feature is that users can select from their list of friends the people they want to share their pictures with, set a timer specific to each person and take a set of pictures that will be automatically shared with these friends in realtime. During registration, Roco has a unique facescanning feature that uses a oneshot learning method combined with geofiltering to allow for facial recognition at higher accuracy. Geofiltering is an efficient filtering method by only queryingfaces with individuals that are close in location. If a picture contains people, the faces in it are automatically recognized and the picture is shared with everyone who appears in the picture. Roco also enables users to share their pictures with people who do not have the application by allowing the users to add nonusersor send out a link of photos they want to share to people by email or phone number.</p>
<p><b>Students:</b> Jenny Hua, Bing Hui Feng, Naren Sathiya, Tu Nguyen, Kasean Herrera</p>
</div>
</li>
<li>2nd Place ($5,000):
<div class="team"><span class="team_name">NoteCycle</span>
<p><b>Project Description:</b> Quicknotes is a Crowdsourced classroom note platform. Students that feel that they take good notes can upload their class notes to Quicknotes and be rewarded. Other students in the class can then see that notes have been posted for this lecture section. Students who wish to view the notes are given a short survey (whenever available) or if not available are asked to pay a micro-transaction on the order of 50 cents per set of notes. After they download the notes, they can rate them so that other students in the class will know which note have found most useful. In order to incentivize students into uploading notes they receive some of the profits for each download of their notes. This also encourages students to post the best possible notes so that they get positive ratings, resulting in more downloads, giving them a larger income.</p>
<p><b>Students:</b> Arvind Sontha, Ofri Harlev, Treston Mendonsa</p>
</div>
</li>
<li>3rd Place ($2,500):
<div class="team"><span class="team_name">FanFeed!</span>
<p><b>Project Description:</b> FanFeed is a free mobile application, available on iOs and Android, that aims to enhance the fan in-stadium experience by helping users avoid long concession stand lines at sporting events by providing in-stadium food delivery and in-seat food purchase. Forget the idea of leaving your seat at that crucial moment that you don&#8217;t want to miss for a beer, some food, or beverages. Fans will now have the opportunity of being able to order food to their seats, no matter where they are located in the stadium. With our efficient delivery system and easy-to-use mobile application, attending a game will never be the same again.</p>
<p><b>Students:</b> Candace Wu, Ravi Patel, Brandon Troung</p>
</div>
</li>
</ul>
</div>

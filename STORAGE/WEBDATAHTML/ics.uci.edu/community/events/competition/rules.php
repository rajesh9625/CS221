Butterworth Product Development<br/> Competition in ICS
<br/>and<br/>
Beall Student Design Competition in Engineering
</a>
</center>
</div>
<!-- --><!-- --><!-- -->
<p>
<b>COMPETITION RULES AND GUIDELINES</b>
</p>
<p>
<b>Participants</b> <br/>
Both competitions are open to all UC Irvine students. Teams must be composed of 
at least two (2) students, of which at least one (1) must be an ICS student (for 
the Butterworth Product Development Competition) and one (1) must be an 
Engineering student (for the Beall Design Competition). For example, a team 
member from the Merage School of Business could contribute business planning 
knowledge that would help the team or students from schools of science or engineering 
could contribute knowledge of a particular application or technology. Students can 
be graduate or undergraduate, but must be registered at UCI for the duration of the 
academic year of the competition.
</p>
<p>
<b>Guidelines</b> <br/>
To participate in these competitions, your team must fill out the Web-based Intent to Enter 
form indicating participation interest. Upon completion of this form, teams will 
be assigned industry/faculty mentors according to the criteria delineated in the 
registration form.
Participants can enter both the Butterworth Product Development Competition 
and also the Beall Student Design Competition, but can only win one (1) prize. 
Entering both competitions increase the chance of winning, but must qualify with 
the appropriate student team members (1 ICS and 1 Engineering student to qualify 
for both competitions).
</p>
<p>
<b>Obtaining Additional Team Members:</b> Potential participants are encouraged 
to meet students with similar interests who may be willing to collaborate and 
form a team at one of the school based workshops, information sessions or the 
Merage School&#8217;s Matrix Mixers that are offered. Students from across campus are 
encouraged to attend even if they do not have a concept developed.
</p>
<p>
<b>Team Integrity:</b> Once the Concept Paper has been submitted, 50% of the team must 
remain intact for the rest of the competition. 100% of the team must remain intact 
after submitting the Product Specification/Proof of Concept.
</p>
<p>
<b>Product Plan/Idea:</b> Teams must present an original idea or current design 
problem, and be in a "pre-incubator" form. They must not be backed by incubators, 
existing companies, venture capitalists or other investors. Teams or team members 
that have received any form of venture capital financing for their competition 
entry plan or a likeness thereof may not participate in the competition. Any team 
receiving venture capital funding while participating in the competition will be 
disqualified.
</p>
<p>
<b>Workshops:</b> Workshops will be offered throughout every phase of the competition. 
Topics will include, 
</p>
<ul>
<li>&#8220;Working with Your Mentor/How to Define a Product&#8221; 
   </li>
<li>&#8220;How to Incorporate a Business Case into your Proposed Product&#8221;
   </li>
<li>&#8220;How to Demo Your Product&#8221;
   </li>
<li>&#8220;Who Owns What&#8230; Intellectual Property at UCI&#8221;
</li>
</ul>
<p>
<b>Documentation, Presentations and Demos:</b> Documents should be single spaced 
with a font size between 10 and 12 points. One (1) oral presentation will be due 
mid-way through the program that will offer proof of concept, feasibility and 
specifications. A demo and presentation are required for the final phase of the 
competition. Both oral presentation and final demo will be presented before the 
judges of the competition. Working prototypes are suggested for the final demo.
</p>
<p>
<b>Awards:</b> Final awards will be presented by the Dean of the School and the Judge 
Chair following the final demo at an Awards Ceremony.
</p>
<ul>
<li>Cash awards will be given to the top three (3) teams for each competition in amounts of $7,500, $5,000 and $2,500.
   </li>
<li>The top three (3) teams for each competition will be offered a Meet &amp; Greet with UCI&#8217;s Office of Technology Alliances to discuss product, patents, copyrights and trademarks and to answer any questions they may have.
   </li>
<li>All teams receiving an award will qualify to participate as a sponsor in a project course in order to help develop their products. Project submissions must occur within the academic year immediately following the competition (certain restrictions apply).
   </li>
<li>First place winners are recognized by presenting their product in the ICS and Engineering school&#8217;s premiere student showcase event, INGENUITY.
   </li>
<li>First place winners will have the opportunity to meet with local investors.
   </li>
<li>All teams will have access to UCI&#8217;s Antrepreneur Center to further develop their concepts/ideas and can be directed to other avenues to explore entrepreneurism.
</li>
</ul>
<p>
<b>Judging Guidelines:</b> The judges are professionals who come from various 
backgrounds. Judges are experts in the process of starting a business including 
business plan developments, or in the technologies the students are developing. The 
judges consider many different criteria when evaluating concept papers and the 
business case. Some of the elements that are considered are as follows:
</p>
<ul>
<li>Does the technological/design innovation provide a sustainable, competitive advantage?
   </li>
<li>Does a competitive advantage exist over other products in this application?
   </li>
<li>Does the functionality satisfy the application?
   </li>
<li>Is there a significant return on investment to user?
   </li>
<li>Is the product description clear, detailed and descriptive?
   </li>
<li>Is there a market need for this product?
   </li>
<li>Has the user been realistically identified?
   </li>
<li>Are the benefits to the user clear and sufficient to result in purchase?
   </li>
<li>Have specific user interfaces been provided?
   </li>
<li>Is the user interface well described?
   </li>
<li>Are the requirements thorough, realistic and accurate?
   </li>
<li>Is there a block diagram?
   </li>
<li>Have all of the major modules been described in detail?
</li>
</ul>


</div>

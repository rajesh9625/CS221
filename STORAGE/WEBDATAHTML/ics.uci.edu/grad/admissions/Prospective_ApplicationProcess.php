<div id="content_title">Graduate Application Process</div>

<blockquote>
<div id="content_text">

<p>Online applications for graduate admissions are accepted from September 1st to December 15th. All materials as outlined below should be submitted <strong>before the December 15th deadline</strong>. (Additional information for those re-applying to the Bren School can be found <a href="Prospective_Re-applying.php" target="_blank" title="ICS Student Affairs: Re-applying for Graduate Program">here</a>.)</p>
<p>You can still apply to ICS if your undergraduate degree is not in the same area as the graduate degree in which you are interested. However, it is helpful if you have taken courses in computer science and math, and/or have some related work experience.</p>
<p><em>ICS accepts applications for admission beginning in the fall quarter only</em>.</p>
<h3><strong><span style="color: #6699cc;"><strong>&#187;</strong></span> Online Application</strong></h3>
<p>Complete the <a href="https://apps.grad.uci.edu/ogsa//index.cfm?action=log_in&amp;CFID=13244617&amp;CFTOKEN=90684455" target="_blank" title="Applicaton for Graduate Admission">online application</a>. Pay particular attention to the following:</p>
<ul>
<li>Enter the appropriate codes for ICS. Note that <em>you must choose either M.S. or Ph.D.</em> as ICS does not have a M.S./Ph.D. program.</li>
</ul>
<blockquote>
<ul>
<li type="circle">Computer Science, <strong>Ph.D.</strong></li>
<li type="circle">Computer Science, <strong>M.S.<br/></strong></li>
<li type="circle">Informatics, <strong>Ph.D.</strong></li>
<li type="circle">Networked Systems, <strong>Ph.D.</strong></li>
<li type="circle">Networked Systems, <strong>M.S.<br/></strong></li>
<li type="circle">Software Engineering, <strong>Ph.D.</strong></li>
<li type="circle">Software Engineering, <strong>M.S.</strong></li>
<li type="circle">Statistics, <strong>Ph.D.</strong></li>
<li type="circle">Statistics, <strong>M.S.</strong></li>
<li type="circle">Information and Computer Science,&#160;<strong>M.S.</strong></li>
</ul>
</blockquote>
<ul>
<li>Enter a specific area of interest. More information about research areas can be found <a href="../../faculty/area/index.php">here</a>.</li>
<li>Save your application ID and student ID numbers; they will be asked of you when filling out other documents.</li>
<li>Applications are processed after the application fee ($90 for domestic applicants, $110 for foreign applicants) has been received. The application fee canot be waived.</li>
<li>You can apply for more than one program simultaneously, but you must fill out a separate application and pay a separate fee for each program.</li>
<li>If you have questions regarding your electronic application or your payment, contact UCI Graduate Division at (949) 824-4611 or <a href="mailto:ogs@uci.edu">ogs@uci.edu</a>.&#160;</li>
</ul>
<h3><strong><span style="color: #6699cc;"><strong>&#187;</strong></span> GRE and TOEFL Scores</strong></h3>
<p>Arrange to have your official test scores sent from the testing agency to UC Irvine. Photocopies or scans will not be accepted.</p>
<p><strong><em>GRE</em></strong></p>
<ul>
<li>The General GRE is required of ALL applicants.</li>
<li>For official GRE General test scores, use <strong>Institutional code: 4859</strong></li>
<li>There is no minimum GRE score.</li>
<li>The Computer Science Subject GRE is recommended for applicants who do not have a degree in Computer Science or a related field.</li>
<li>GRE scores are good for five years, after which time the exam must be retaken.</li>
<li>We cannot accept GMAT scores in lieu of GRE scores.</li>
</ul>
<blockquote/>
<p><strong><em>TOEFL</em></strong></p>
<ul>
<li>The TOEFL (Test of English as a Foreign Language) is required of international applicants.</li>
<li>For official TOEFL test scores, use <strong>Institutional code: 4859</strong></li>
<li>The minimum TOEFL score is 80. For more information, please see <a href="http://www.grad.uci.edu/admissions/applying-to-uci/english-proficiency.html" target="_blank" title="Graduate Division English Proficiency">Graduate Division's English Proficiency site</a>.</li>
<li>UCI will only waive the TOEFL requirement for applicants who have completed ALL requirements for a B.S., M.S. or Ph.D. degree in the U.S. prior to submitting their application.</li>
</ul>
<h3><strong><span style="color: #6699cc;"><strong>&#187;</strong></span> Letters of Recommendation</strong></h3>
<p><em>Three</em> letters of recommendation are required, but you may submit as many letters as you wish.</p>
<p><strong><em>For paper submission</em></strong></p>
<p style="padding-left: 30px;">Provide the Letter of Recommendation and Waiver of Access forms to at least three people you have identified who can evaluate your academic and/or professional achievements, describe your strengths and weaknesses and comment on your character, integrity and motivation. Your recommenders should mail his/her letter directly to:</p>
<p style="padding-left: 50px;">ICS Graduate Office<br/>Donald Bren School of Information and Computer Sciences<br/>352 Information &amp; Computer Science Bldg.<br/>University of California, Irvine<br/>Irvine, CA 92697-3430</p>
<p><strong><em>For online submission</em></strong></p>
<p style="padding-left: 30px;">Applicants who have completed and submitted their online application should send their recommenders a link to that page, along with their application ID number.</p>
<h3><strong><span style="color: #6699cc;"><strong>&#187;</strong></span> Personal Statement</strong></h3>
<p>The Personal Statement (same as Statement of Purpose) should be submitted via the online application.</p>
<h3><strong><span style="color: #6699cc;"><strong>&#187;</strong></span> Official Transcripts</strong></h3>
<p>Arrange to have one copy of your official transcripts from the colleges you attended sent directly to the ICS Graduate Office.</p>
<p>An official English translation is required from international students.</p>
<p>Photocopies, scans, or unofficial copies of transcripts will not be accepted.</p>
<h3><strong><span style="color: #6699cc;"><strong>&#187;</strong></span> Checking Your Application Status</strong></h3>
<p>Applicants may login to the online <a href="https://gats.ics.uci.edu//tracker/" target="_blank" title="GATS Tracker">GATS TRACKER</a> to check for application materials received and their current application status.</p>
<p>Ph.D. and M.S. admissions decisions are ongoing from approximately March through May. Acceptance and denial letters will be sent electronically.</p>
<p>We cannot provide you with the final decision over the phone.</p>


</div>
</blockquote>

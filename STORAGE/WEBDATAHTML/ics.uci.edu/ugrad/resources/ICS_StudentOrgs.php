<h4 class="header">ICS Student Organizations</h4>
<p>ICS students are encouraged to participate in student organizations to complement their academics.&#160; Below is a list of student organizations within the Donald Bren School of ICS.&#160;</p>
<h2>ICS Student Council</h2>
<p>The <a href="https://student-council.ics.uci.edu/" target="_blank">ICS Student Council</a> is the official student government for the Donald Bren School of Information and Computer Sciences. Its aim is to improve the lives of students academically, professionally, and socially. The ICS Student Council hosts career information nights with companies such as Google, Blizzard, Accenture and numerous others; organizes training sessions in which students teach each other about new and upcoming technologies; and performs resume-writing and interview-training sessions. In addition, it is responsible for a series of very popular social events, including ICS Day (featuring games, coding competitions, dance crew performances and the infamous dunk tank and inflatable jousting), reverse career fairs (where students demo their work to prospective employers) and app jams (themed competitions to design and implement new apps).</p>
<h2>iCAMP</h2>
<p><a href="http://www.math.uci.edu/%7eicamp/" target="_blank" title="Interdisciplinary Computation and Applied Mathematics Program">Interdisciplinary Computation and Applied Mathematics Program</a> provides an opportunity for undergrads to learn about a topic in computational applied mathematics. Opportunities are also available for paid summer research.</p>
<h2>WICS</h2>
<p>The mission of <a href="http://wics.ics.uci.edu/" target="_blank">Women in Computer Sciences</a> is to encourage women to pursue a college degree and a successful career in the computer science field by offering the resources they need to excel in their degrees from freshman to senior year. WICS provides professional and technical workshops, academic tutoring, leadership and team building projects, as well as numerous opportunities for women to connect with companies and alumni. It hosts peer-networking and mentorship events to foster community and to inspire collaboration on extracurricular projects.</p>
<h2>ACM UC Irvine Student Chapter</h2>
<p>The <a href="www.acm.org" target="_blank">Association for Computing Machinery</a> is the world&#8217;s largest society of information technology students and professionals. The <a href="http://acmuci.com/" target="_blank">UC Irvine student chapter</a> represents the needs of the UCI computing community, and hosts regular workshops to share knowledge and hosts presentations by distinguished speakers about new ideas. The chapter also is actively engaged in preparing teams for various programming competitions, such as the <a href="http://icpc.baylor.edu/" target="_blank">ACM Intercollegiate Programming Competition</a> and <a href="http://www.ieee.org/membership_services/membership/students/competitions/xtreme/index.html" target="_blank">IEEExtreme</a>.</p>
<h2>INSA</h2>
<p>Informatics Student Association works to unite the Informatics community at UCI and to promote the Informatics major to students and companies alike. We offer workshops, corporate visits and tours, individual tutoring and mentoring, tournaments and competitions and networking/recruiting events with technology companies.</p>
<h2>UCF</h2>
<p>Undergraduate Computing Federation promotes interaction between and provide resources to students with an interest in Unix, Linux, FreeBSD, Network Security, Cryptography, or Ethical Hacking. Open to all UCI undergraduate or graduate students.</p>
<h2>VGDC</h2>
<p>The <a href="http://vgdc-uci.com/" target="_blank">Video Game Development Club</a> is dedicated to anyone interested in video games and game development, with the particular objective of supporting students in developing portfolios, enhancing skills and connecting to industry professionals. VDGC always has an active set of projects on which anyone can choose to work to learn new game design techniques, hone artistic skills or simply learn about new game development platforms. The club holds weekly workshops on topics such as 3D art, design, programming and project management. It also organizes the annual Game Developers Week, which is UCI&#8217;s very own implementation of the Game Developers Conference (GDC), featuring speakers from game companies in the area (e.g., Obsidian, Blizzard).</p>
<h2>MAISS</h2>
<p><a href="http://www.maissuci.com/index.html" target="_blank">Management Information Student Society</a> is tailored to the interests of <a href="http://www.informatics.uci.edu/undergrad/bs-business-information-management/">business information management</a> majors at UCI as well as those interested in business and technology. MAISS aims to provide professional opportunity, an academic community and a strong social network. It offers training sessions, holds beach bonfire, and organizes a popular annual spring banquet. MAISS regularly brings in professionals to talk about such topics as consulting, startups and entrepreneurship.</p>



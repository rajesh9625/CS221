<p><strong>DEGREES AVAILABLE: Major </strong></p>
<p><img alt="photo:: data science student" class="left" height="156" src="http://farm6.static.flickr.com/5684/22202302366_d15fd1c454_b.jpg" width="200"/></p>
<p>Data science can be thought of as the &#8220;science of data analysis&#8221;: developing mathematical models and algorithms for extracting useful information from data, combining statistical thinking with computational methods. We are living in an increasingly data-driven world, with rich and complex data available from sensors in our mobile phones, from our social networks, from our cars, our homes, from the Web, from new scientific instruments, and more. One of the key questions of our age is how to use of all of this data to make effective predictions and decisions. How can our smart devices learn our preferences and make recommendations? Can we develop more intelligent Web search engines that learn from data? How can statistical models take advantage of modern genomics to develop personalized medicine? How can climate scientists harness the wealth of satellite data to better understand the planet? Can we predict the stock market? These are just a fraction of the questions that drive data science. This degree program teaches students the underlying principles of how to organize, model, and analyze data in a systematic fashion and how to apply these principles to a wide variety of real-world problems.</p>
<p><cite><a href="#DS1"> &#8250; Is Data Science for me?</a><br/> <a href="#DS2">&#8250; What Courses will I take?</a><br/> <a href="#DS3">&#8250; Career Paths: What can I do with This Degree</a><br/> <a href="#DS4">&#8250; Why study Data Science at UC Irvine?</a><br/> <a href="#DS5">&#8250; How can I enroll in the Major?</a><br/><a href="Data_Science_15-16.pdf" target="_blank">&#8250; Sample Program of Study for the Data Science Major</a><br/><a href="#DS6">&#8250; Additional Information related to Data Science</a></cite></p>
<a id="DS1"></a>&#65279;<br/>
<h4><a href="#DS1"></a>Is Data Science for me?</h4>
<p>If you enjoy the combination of working with data, understanding basic mathematical principles, and implementing your ideas in algorithms and software, then the Data Science major is well worth considering. The Data Science major has a dual emphasis on the principles of both statistics and computation. As a student you will learn the principles underlying mathematical and statistical aspects of data analysis as well as a broad range of foundational skills in computing. The program will build on these ideas to teach you how to utilize your knowledge to analyze and solve a variety of data analysis problems.</p>
<p>&#8593; <a href="http://www.ics.uci.edu/ugrad/degrees/degree_datascience.php#startcontent">Back to top</a> <a id="DS2"></a>&#65279;</p>
<h4>What Courses will I take?</h4>
<p>In the first two years of the program, students take core courses in both the Statistics and Computer Science Departments, providing a strong foundation in the principles of each field. In the third and fourth years of the program, students take more specialized courses on topics such as design of algorithms, machine learning, statistical modeling, information visualization and Bayesian statistics. A major component of this degree is the final year capstone project course, which is a two-quarter course that teaches students how to apply statistical and computational principles to solve large-scale, real-world data analysis problems. For a full list of required and elective courses see the description of the <a href="http://catalogue.uci.edu/donaldbrenschoolofinformationandcomputersciences/departmentofstatistics/#majortext" target="_blank">Data Science Major in the UCI General Catalogue</a>.</p>
<p>Students are encouraged to consult an academic advisor in the Bren School of ICS to determine the coursework designed to meet their educational goals.</p>
<p>&#8593; <a href="http://www.ics.uci.edu/ugrad/degrees/degree_datascience.php#startcontent">Back to top</a><br/> <a id="DS3"></a>&#65279;</p>
<h4>Career Paths: What can I do with this Degree?</h4>
<p>Demand for graduates with skills in both statistics and computer science currently outpaces supply - the McKinsey Global Institute forecasts that by 2018 the United States may face a shortage of 1.5 million individuals with the skills to analyze and use data effectively. Thus, students with data science skills typically find employment quickly, across a wide variety of sectors, including software and internet companies (e.g., Google, Facebook, Apple, Microsoft), finance (banking, financial investment and insurance), engineering, and more. Data Science graduates are well-qualified for job titles such as data scientist, data analyst or statistician, both in the public and private sectors. Graduate school in areas such as Computer Science or Statistics is also a possible career path leading to advanced research careers in industry and in government, in addition to being qualified for university faculty positions.</p>
<p>&#8593; <a href="http://www.ics.uci.edu/ugrad/degrees/degree_datascience.php#startcontent">Back to top</a><br/> <a name="DS4"></a></p>
<h4>Why study Data Science at UC Irvine?</h4>
<p>UC Irvine is the only university in the UC system, and one of the few in the world, where statistics and computer science are co-located in the same school and building. The Data Science major takes full advantage of this synergy, providing an integrated experience for our students at the interface of computing and statistics. You will be taught by faculty who are world-class researchers in areas such as machine learning, Bayesian statistics, database management, graph algorithms, and more. You will have opportunities to participate in undergraduate research projects with our faculty and to take part in summer internships in Southern California or Silicon Valley. And when you graduate you will be able to take advantage of our many connections to startups, to major corporations, and to graduate schools, who are well aware of UC Irvine&#8217;s strengths in Data Science.</p>
<p>&#8593;<a href="http://www.ics.uci.edu/ugrad/degrees/degree_datascience.php#startcontent">Back to top</a></p>
<a name="DS5"></a>
<h4>How can I enroll in the Major?</h4>
<p>In addition to material below, you are also encouraged to visit <a href="http://www.ics.uci.edu/ugrad/degrees/index.php" target="_blank">ICS Majors Webpage</a> for further information. Note that the earliest that you will be able to complete this degree is Spring 2018.</p>
<strong>Prospective Freshmen</strong>: If you are a prospective freshman or transfer student looking for more information on applying to the Data Science undergraduate program, please visit the<strong> <a href="http://www.ics.uci.edu/prospective/en/admissions/freshmen/" target="_blank">Undergraduate Admissions website</a></strong>. <br/><br/><strong>Transfer Students</strong>: If you are a prospective transfer student please visit the <a href="http://www.ics.uci.edu/prospective/en/admissions/transfer/" target="_blank">UCI Bren School admissions page for transfer students </a>and please also consult the section on <a href="http://catalogue.uci.edu/donaldbrenschoolofinformationandcomputersciences/departmentofstatistics/#majortext">Transfer Applicants for the Data Science Major in the UCI General Catalogue.</a> <br/><br/><strong>Enrolled UCI Students</strong>: If you are a currently-enrolled UCI student you can transfer into this major if you satisfy the following requirements for a Change of Major: <br/>
<ul>
<li>Cumulative UC GPA of 2.7 or higher</li>
<li>3.0 or higher average GPA and no grade lower than a C for ICS 31, ICS 32, and one of the following: Math 2A, Math 2B, Math 2D, ICS 6B, or ICS 6D.</li>
<li>As of Academic Year 2015-2016 we are accepting students at either the freshman or sophomore levels.</li>
</ul>
<br/>If you are interested in a change of major, meeting with an ICS academic counsellor is strongly recommended. For general University information on a change of major see the <a href="http://www.changeofmajor.uci.edu/" target="_blank">UC Irvine Change of Major Criteria</a>.
<p>&#8593;<a href="http://www.ics.uci.edu/ugrad/degrees/degree_datascience.php#startcontent">Back to top</a></p>

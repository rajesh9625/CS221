Welcome from Dean Stern






Welcome to the Donald Bren School of Information and
Computer Sciences (ICS) at UC Irvine. As the only computing-focused school in the University of California
system, the Bren School is a unique place for study, research and collaboration. Our three departments — Computer
Science, Informatics and Statistics — incorporate a broad range of curricular
and research interests across the informational and computational sciences.
The school offers a range of undergraduate majors
(six in all), including recently developed majors in Computer Game Science and Business Information Management. The diverse selection of majors enables
students to gain in-depth knowledge of the information and computer sciences
while developing an understanding of the broad reach of the discipline. Our goal is to give students the real-world
tools necessary to succeed in the ever-changing technology industry.
Our faculty and graduate students are leaders in their research areas which span
information and computer sciences; they obtain corporate and federal extramural
grants, they write leading textbooks in their fields, publish influential
journal articles, and participate in key conferences and workshops.  Research in the information and
computer sciences is applicable across many scholarly and scientific fields.  Indeed, applications to scientific and
policy questions in the biological/health, engineering, and social science
disciplines motivate much of our research.
I invite you to explore our website and learn more about the
Bren School. Please feel free to
contact me at icsdean@ics.uci.edu if
you have questions or comments.
Thanks,


		  Hal SternTed and Janice Smith Family Foundation Dean







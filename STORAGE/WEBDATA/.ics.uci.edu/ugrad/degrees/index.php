Changing from one ICS major to another ICS major

Please watch this short video: How to Change MajorsMeeting with an academic counselor or peer academic advisor is recommended. They will help you determine the most efficient way for you to meet the degree requirements of your new intended major.If you are eligible to change, submit a Change of Major Application online through your Student Access page.

Changing from a Major in another school to ICS

Please watch this short video: How to Change MajorsVisit the UCI Change of Major Criteria website for a listing of specific change of major guidelines for your intended major.Meet with an ICS academic counselor or peer advisor as soon as possible to determine whether you are eligible to switch majors and to map out an academic plan.Once you have met the Bren School of ICS change of major requirements:Submit the Change of Major Application onlineIf you have over 120 units, an academic plan with intended graduate quarter and year must also be submitted.

Changing from ICS to a Major in another school

Visit the UCI Change of Major Criteria website for a listing of specific change of major guidelines for your intended major.Meet with an academic counselor or peer advisor in the school of your intended major; they can help you create an academic plan that will help you meet your goals.









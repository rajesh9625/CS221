          Theoretical computer science aims at understanding the 
mathematical structure of computational problems in order to derive 
efficient and scalable solutions to those problems. 

          Bren School faculty members have made significant 
contributions to many topics in this field, including:
Graph algorithms and graph drawing (computing with systems of pairwise interactions between 
objects such as web page links, protein interactions, or social networks)Computational geometry (computing with planar or spatial data)String and tree algorithms (for indexing and processing large textual and XML documents, and DNA sequence information)Data compression (computationally efficient methods for removing redundancy from stored information)Online algorithms (automated decision making in the face of uncertainty)

For more information visit the ICS Theory 
Group page.






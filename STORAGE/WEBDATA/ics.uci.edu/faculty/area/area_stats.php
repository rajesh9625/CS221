In recognition of the interdisciplinary nature of statistics on
            the computational field, UCI created
            an independent Department of Statistics that joined the Bren
            School of ICS in July 2003. 
An emerging
            field
            of study in information and computer sciences, Statistics faculty members bring with them a strength
            in statistical theory and a focus on the development of statistical
            methods for solving problems.
Researchers at UCI are concerned with developing and studying methods for collecting, analyzing, interpreting and presenting empirical data. Statistical principles and methods are important for addressing questions in public policy, medicine, industry and virtually every branch of science.






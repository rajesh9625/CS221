Ubiquitous Computing








 
Research Faculty» Paul  Dourish
» Gillian R Hayes
» Alfred  Kobsa
» Cristina V. Lopes
» Donald J Patterson
» Bill  Tomlinson
Ubiquitous computing refers to the creation and deployment of
          computing technology in such a way that it becomes an invisible part
          of the fabric of everyday life and commerce. 
In the ubiquitous computing
            era, computers in the traditional sense gradually fade from view. Information
            and communication mediated by computers is available anywhere and anytime
            through devices that are embedded in our environment, completely inter-connected,
            intuitive, effortlessly portable and constantly available. 
Users share
            information and communicate continuously throughout the day. In short,
            ubiquitous computing provides computing for all - access to anything,
            by anyone, at anytime, anywhere. 
Advances in wireless networking and the Internet, embedded systems
            and human-computer interaction are the forerunners for ubiquitous
            computing. It is the convergence of these and other technologies
            that brings about the trend towards ubiquitous computing. 
Ubiquitous
            computing places considerable requirements on both hardware and software
            development and support, increasing needs for information and data
            analysis, and also leads to new applications that improve society.
Ubiquitous computing builds upon and unites virtually all of the
            current research strengths in the Bren School. With particular emphasis on ubiquitous
            computing, the Bren School  faculty are addressing issues such as context-aware
        computing, whereby mobile computing responds to one's current context.






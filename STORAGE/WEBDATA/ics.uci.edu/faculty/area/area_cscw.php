Information technologies bring people together -- through social 
networking, through collaborative systems, through digital media,
             and through communications. Informatics has been a 
long-term leader in the study of social engagement through information 
systems.
             Topics include distance collaboration, workflow and 
process-based systems, multi-user gaming, and cultural engagements.






» Gary M. Olson
UC Irvine is an acknowledged center for the study of social 
informatics, which incorporates the social and cultural aspects of
             information technology development and use. 

          Social informatics employs techniques and theories from 
social sciences and cultural studies to understand the shaping and
             applications of digital media and their organizational, 
political, historical, and economic contexts. 

          This topic links information system analysis with design. 






Scientific Computing refers to the application of computers to 
scientific problems, from astrophysics to zoology. 

			   The mode of application can be system modeling, data analysis 
and mining, or visualization. 

			   The focus can be on developing new computational techniques, 
such as parallel algorithms or new data mining ideas, or on the
			      novel application of existing techniques to new scientific 
problems. 

			   A number of our faculty are focussed primarily on 
bio-informatics, so in addition to the links below, more information can
 be
			      found about bio-informatics at UCI by checking out
			      IBAM
			      and
			      IGB.






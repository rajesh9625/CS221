  There are a number of significant problems in biology and medicine for which
  computational approaches can yield important results. 
The worldwide efforts to
            construct databases of protein and small molecule structures, DNA sequences,
            metabolic pathways, regulatory mechanisms, pharmaceutical structures and activities,
            patient response data, etc., have created many opportunities for intelligent
            systems. 
At UCI, current areas of research in bio/medical informatics include:Medical Information Access
                Knowledge Representation for Health-Care Guidelines
                Structure in Biomedical Data
                Biomedical Simulations
                Discovery of Gene Expression Control
                Knowledge Discovery in Clinical Databases
                Computational Biology
                Bioinformatics and Probabilistic Modeling.






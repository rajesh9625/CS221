Security, Privacy, and Cryptography








 
Research Faculty» Alfred  Kobsa
» Gene  Tsudik
  The development of secure communication protocols
    is a critical issue in today's age of pervasive communication. 
Bren School  research
            in this area includes anonymity and authentication in network security, key
            agreement and digital signatures in cryptography, and security issues in electronic
            commerce. 
Researchers also are designing high-performance algorithms and
            data structures for solving large-scale problems motivated from information
            assurance and security, the Internet, information visualization and geometric
            computing.
More information: http://sconce.ics.uci.edu/






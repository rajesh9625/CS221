Computer Graphics and Visualization








 
Research Faculty» David  Eppstein
» Michael T. Goodrich
» Aditi  Majumder
» Gopi  Meenakshisundaram
» Bill  Tomlinson
» Shuang  Zhao
Focuses on the field of visual computing that deals with 
generating/capturing, representing, rendering and interacting with 
synthetic
      and real-world images and video. We work on end-to-end solutions 
from capturing of images and geometry; representing large geometric,
      image, and video data sets; geometry and image processing; 
interactive access and rendering of large visual data sets; algorithms 
for
      building large area immersive displays for the presentation of 
visual content; and interaction techniques in both small personal 
displays
      and in large displays for collaborative environments. 

   Current and future research areas include, but are not limited to:

      Designing new cameras with advanced capabilities for 
capturing real-world scenes.
         Image processing for enhanced visual content quality
         Computational geometry and graph algorithms for 
geometry processing
         Interactive rendering of very large geometric data 
sets.
         Ubiquitous displays -- Reconfigurable multi-projector 
display systems.
         Interaction techniques for large area displays.
         Sketch based modeling and interaction for personal 
displays.
      

   Applications are Internet-based visualization of remotely located 
complex objects, volume manipulation and visualization, geographic
      information systems, and simplified 3-D rendering based on 2-D 
images.More information: http://graphics.ics.uci.edu/



			






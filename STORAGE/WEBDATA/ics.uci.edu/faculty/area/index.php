ICS Research Areas




Curiosity about the world and a commitment to solving problems are the passions that drive ICS faculty. Their research in the information and computer sciences are applicable to many scholarly and scientific fields. But our faculty don't do it alone, students work side-by-side with nationally renowned professors to advance knowledge and improve lives. Below is a list of ICS research areas:
Algorithms and Complexity Bren School faculty members have made significant contributions to many topics in this field, including graph algorithms and graph drawing (computing with systems of pairwise interactions between objects such as web page links, protein interactions, or social networks) and computational geometry (computing with planar or spatial data).
Artificial Intelligence and Machine Learning Research in AI is concerned with producing machines to automate tasks requiring intelligent behavior. Examples include computer vision, bioinformatics, constraint-based problem solving, text understanding, data mining and smart sensor networks.
Biomedical Informatics and Computational Biology  Involves the use of techniques from applied mathematics, informatics, statistics, and computer science to solve biological problems. Current areas of research at the Bren School include medical information access and knowledge representation for health-care guidelines.
Computer Architecture and Design Develops methods and tools for ensuring the reliability and quality of complex, large-scale software systems. These methods and tools support the development, deployment, maintenance and evolution of complex software systems.
Computer Graphics and Visualization Focuses on the field of visual computing that deals with generating/capturing, representing, rendering and interacting with synthetic and real-world images and video. We work on end-to-end solutions from capturing of images and geometry; representing large geometric, image, and video data sets; geometry and image processing; interactive access and rendering of large visual data sets; algorithms for building large area immersive displays for the presentation of visual content; and interation techniques in both small personal displays and in large displays for collaborative environments.
Computer-Supported Cooperative Work Information technologies bring people together -- through social networking, through collaborative systems, through digital media, and through communications. Informatics has been a long-term leader in the study of social engagement through information systems. Topics include distance collaboration, workflow and process-based systems, multi-user gaming, and cultural engagements.
Computer Vision Computer vision at UCI focuses on understanding the information processing capabilities of biological visual systems and on developing computational systems for processing visual media. Research spans both theoretical questions of perception and object representation as well as practical applications ranging from automated surveillance to biological image analysis. 
Databases and Data Mining Focuses on research related to architectures, index structures, algorithms, models, and performance evaluation of a variety of next-generation databases and information systems and technologies for data mining.
Embedded Systems Focuses on issues relating to embedded systems, a special-purpose system in which software and hardware computing elements are completely encapsulated by the device or environment it controls. Unlike a general-purpose computer, such as a personal computer, an embedded system performs pre-defined tasks, usually under very specific constraints (e.g, low power) and requirements (e.g., reliability). 
Environmental Informatics Humanity is currently facing a range of significant environmental challenges such as global warming, species extinction, pollution, and overpopulation. Informatics tools and techniques can help facilitate responses to these challenges, and assist with planning for future environmental issues. 
Human Computer Interaction HCI research at UCI stretches from the architecture of novel interactive systems to the social and cultural considerations of information technology adoption and use. We employ laboratory, ethnographic, and prototyping techniques to understand how people adopt, adapt, and respond to information systems. Recent research has investigated privacy issues in mobile systems, tangible interfaces for group awareness, interactive animation, and visualization of location information. 
Medical Informatics This topic concerns the development and application of information systems to healthcare. Information systems have a critical role to play in contemporary health and wellness programs. This includes technology in hospital settings but also persuasive technologies for healthy living, health care in the home and in the community, and in the interactions between partners in the health care system. 
Multimedia Computing Multimedia computing started receiving attention more than a decade ago.  Naturally, early systems dealt with very limited aspect of multimedia.  With progress in technology, several computing addresses important issues in creation, communication, storage, access, and presentation of information and experiences.  In our department, we are addressing research issues in fundamentals of multimedia systems and their advanced applications. 
Networks and Distributed Systems Researchers investigate various issues in the design and analysis of high-speed networks for multimedia applications. They are actively involved in research on computer networks and distributed systems, with the goal of designing, analyzing and implementing communication systems that allow high-speed transport of multimedia information between end-users. 
Operating Systems The operating systems area at UCI embraces a wide range of topics related to theory and practice of computer systems software. Researchers here are building systems for reliable and efficient big data processing, mobile I/O virtualization, program analyses and various other applications.
Programming Languages and Systems Systems software research at UCI has expanded to include topics such as program restructuring and transformation techniques for parallelization and distribution, compiler-assisted memory management, component-oriented languages and dynamic code optimization. 
Scientific and Numerical Computing Refers to the application of computers to scientific problems, from astrophysics to zoology. The mode of application can be system modelling, data analysis and mining, or visualization. The focus can be on developing new computational techniques, such as parallel algorithms or new data mining ideas, or on the novel application of existing techniques to new scientific problems.
Security, Privacy and Cryptography Bren School research in this area includes anonymity and authentication in network security, key agreement and digital signatures in cryptography, and security issues in electronic commerce.
Social Informatics UC Irvine is an acknowledged center for the study of social informatics, which incorporates the social and cultural aspects of information technology development and use. Social informatics employs techniques and theories from social sciences and cultural studies to understand the shaping and applications of digital media and their organizational, political, historical, and economic contexts. This topic links information system analysis with design.
Software Engineering Software research at UCI is aimed at creating new software technology and solutions, furthering the information revolution. The central goal of this research is improvement in software development, evolution, deployment, quality, understandability and cost-effectiveness.
Statistics and Statistical Theory Researchers at UCI are concerned with developing and studying methods for collecting, analyzing, interpreting and presenting empirical data. Statistical principles and methods are important for addressing questions in public policy, medicine, industry and virtually every branch of science.
Ubiquitous Computing Ubiquitous computing builds upon and unites virtually all of thfe current research strengths in the Bren School. Researchers are addressing issues such as context-aware computing, whereby mobile computing responds to one's current context.






Butterworth and Beall competitions reward the best in both hardware and software











More about the 2014 Finalists (alphabetically by team name)



AntTech 
Tim Kelly, Thomas Gheorghe, Dalen Ellis. Mentor: Channa Samynathan (B.S. ’88, ICS), 
solutions engineer, Syniverse Technologies. Competitions Entered: Beall & Butterworth. 
The Air Drum Kit consists of two glove-like hand attachments, one foot mounting and 
a smartphone. Users then have a virtual drum, which they cannot see, but can play by 
simply drumming in the air where drum components and their respective positions 
typically are. 

 
 


BioGuideUCI 
Hana Yamate-Morgan, Nazneen Pashutanizadeh, Joycelin Luc, Sharon Kuruvilla, 
Aleksandar Metulev, Xiaoxuan Zhang. Mentor: David Hsiang, associate professor of 
clinical surgery, School of Medicine. Competition Entered: Beall. 
BioGuideUCI has developed a sutured-guidewire, the S-Wire, enabling soft-tissue 
excisional biopsy procedures to be conducted with greater safety, stability, accuracy and 
objectivity, so that cancer may be diagnosed and treated in its earliest stages.

 
 


Bru Dating 
Govind Rai, Gio Borje. Mentor: Greg Moulton (B.S. ’82, ICS), consultant. Competition 
entered: Butterworth. 
Bru is a mobile dating app that sets you up on an immediate date with someone you’re 
interested in, without messaging or exchanging any information. Tapping into Facebook, 
Linkedin, and Google+ API’s to pull user data to create dating profiles, Bru seeks to 
quickly and safely facilitate in-person dates for our users and to make first dates less 
awkward and stressful.

 
 


Digital Afterlife 
Grace Pai, Nafiri Kusumakaulika, Anita Marie Gilbert. Mentor: Art Hitomi (B.S. ’96, 
M.S. ’04, Ph.D. ’10, ICS), chief technology officer and co-founder, Numecent. 
Competition Entered: Butterworth. 
“Afterword” is an online curator tool that allows users to create a customize memorial for 
their deceased friend or family member using relevant data gathered from various social 
media feeds that the deceased was affiliated to, such as Facebook and Instagram.

 
 


DocBot 
Evan Schein, Michael Tran. Mentor: Andrew Ninh. Competitions Entered: Beall & 
Butterworth. 
“DocBot” is a web-based clinical decision support system (CDSS) that uses patient 
interaction and electronic health records (EHR) analytics to assist medical practitioners to 
make the best decisions. It consists of a preclinical, digital form that allows the patient to 
enter demographic information and symptoms, and a digital interface to provided medical 
practitioners with as much organized information as required. 

 
 


Fruit Mill 
Nathaniel Major, Sivabalan Thirunavukkarasu, Rohan Achar. Mentor: Scott Lein, CEO 
and co-founder, grandPad. Competition Entered: Butterworth. 
“Grapes” is a powerful, versatile Android app that can appeal to a wide range of social 
applications. Focusing on simplicity via short videos and geo-tagging, “Grapes” allows 
any user to create short, minute long (or less) videos and then tag these videos to their 
current locations. Other users can then watch, rate, and favorite these videos, using them 
to learn more about specific locations, provide casual social interaction, and more. 

 
 


HindSight 
Scott Godfrey, David Carrillo, Kemal Davaslioglu. Mentor: Vikash Rugoobur, co-
founder and CTO, Curve Tomorrow. Competitions Entered: Beall & Butterworth. 
HindSight product is a bicycle-mounted proximity-detection and data-gathering device 
intended to provide advance warning to a bicycle rider of approaching vehicles and 
vehicles encroaching on their personal space. 

 
 


Mattina 
Fady Barsoum, Anderson Nguyen, Irvin Huang, Charles Lam, Hong San, Wong. Mentor: 
Channa Samynathan. Competitions Entered: Beall & Butterworth. 
Mattina gives coffee lovers the freedom to make coffee whenever and wherever they 
want with Keurig’s K-Cup coffee pods. (Keurig’s K-Cup coffee pods integrates ground 
coffee, creamer and coffee filter into one compact and convenient capsule.) Mattina is 
aimed at providing the functionality of the existing full-sized Keurig coffee maker in a 
portable and compact form. 

 
 


Nimbl 
Candace Borders, Ron Sahyouni, Thomas Truong, Robhy Bustami. Mentor: Brian Dao 
(BS ’03, ICS), co-founder, Alpha Sprouts. Competition Entered: Butterworth. 
“Nimbl” is a mobile application for the iPad involving game-like tasks designed to 
rehabilitate fine motor functions of the fingers, hands, wrists and arms after injury 
(likely stroke) or surgery. The tasks have been designed to mimic the kinds of motions 
that physical/occupational therapy patients are actually required to do as part of their 
treatment regimen, but in a game-like setting that tracks their progress and sends reports 
to the therapist/rehabilitation physician. 

 
 


Personicle 
Laleh Jalali, Siripen Pongpaichet, Da Huo, Hyungik Oh. Mentor: Ramesh Jain, Bren 
Chair and professor of computer science, ICS. Competition Entered: Butterworth. 
“UCIAsthma” is a smartphone application for managing asthma. Asthma patients and 
health care providers are the users of this product. The front page of “UCIAsthma” 
displays real-time air quality and air pollution forecast based on the current location of 
the user, allowing users to limit their outdoor activities accordingly. 

 
 


Racket Buddy 
Kenneth Gutierrez, Alecxandr Andres, Anthony Christensen. Mentor: David 
Reinkensmeyer, professor of anatomy and neurobiology, Engineering. Competition 
Entered: Beall 
Swing Thing is an electronic device that is inserted inside the throat of a tennis racket 
that provides feedback through tennis gameplay through visual and audio feedback. 
The purpose of the device is to allow users to improve their tennis strokes without the 
presence of the coach. The device senses the motion of the racket and analyzes it in order 
to provide feedback to the user. 

 
 


SEAMS 
Maunika Gosike, Sonia Kaushal, Everardo Camacho, Ariel Beroukhim, Shalom 
Stevenson. Mentors: Michael Klopfer, Raj Khalsa. Competition Entered: Beall. 
This cost-effective, magnetic-based tracking system that verifies that the body is clear 
of retained surgical sponge (RSS). This product would be used by the surgical staff in 
the operating room prior to closing the open cavity after surgery. It will be a simple and 
efficient final check of the body to make sure it is clear of RSS to reduce the number 
of X-rays necessary in the operating room, thus reducing the overall expenses for the 
hospital and patient. 

 
 


Team Iron Fist 
Nate Directo, Joseph Cuevas, Kenneth Gutierrez. Mentor: Binh Dang (B.S. ’97, ICS), co-
founder & managing partner, MeridianLink. Competition Entered: Beall. 
Wax On is a waxing iron that allows users to apply hot wax to their skis and snowboards 
in a clean, effective, and timely manner. Wax On functions on the premise of three basic 
principles: internally melt ski wax; contain molten ski wax; and deliver molten ski wax 
upon demand. These principles deliver the user a much more streamlined approach to 
waxing their snowboards or skis. Beyond the reduction of the user’s time and effort, Wax 
On also guarantees an effective waxing by providing the user with the option to adjust the 
temperature of the iron to the optimal operating temperature of their wax. 

 
 


Turtle Writing 
Suhang Jiang, Leonard Bejosano, Yuan Ning, Youngmin Park, Jiayi Shen, Debbie Le Yu, 
Melissa Niiya, Dakuo Wang. Mentors: Mark Warschauer, associate dean and professor of 
education, School of Education; and Alex Ihler, associate professor of computer science, 
ICS. Competition Entered: Butterworth. 
Turtle Writing is an online platform that provides a community for English writing 
professionals and learners to connect with each other through a crowdsourcing approach 
in order to match the best professional writer for a specific learner’s needs to create the 
most efficient learning experience. 

 
 


Vertai 
Amanda Nozaki, Isabella Samra, Kevin Lam. Competition Entered: Beall  
Vertai is a new brand of footwear that will be capable of transforming between a sandal 
and a closed-toe shoe. The original concept came from the frustration of bringing a 
change of shoes whenever one had to switch between business attire to casual attire for 
working individuals, but the design team realized children might be a better market for 
this product.







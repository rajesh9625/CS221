ABOUT hITEC
The ICS Technology and Entrepreneurship Competition (hITEC) is the cornerstone of the Bren School entrepreneurship program. 
The competition is designed to foster a spirit of entrepreneurship among Bren School and UC Irvine students, and fuel the development of new technologies that have the potential to positively impact the marketplace.Benefits
			   Gain experience using your skills to turn an idea into a 
potentially viable product. 
			   Compete for $9,000 in prizes.

			   Notice

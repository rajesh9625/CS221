About the Bren gift




The Donald Bren School of Information and 
Computer Sciences was made possible by the generous $20 million gift from Orange 
County business leader and philanthropist Donald Bren, chairman of The Irvine 
Company.
Mr. Bren's gift equals the largest single gift ever to UC Irvine and has 
created the most endowed faculty positions at one time on any University of 
California campus.
In recognition of this transformational gift, the school, renowned worldwide 
for leading the innovation of new information and computing technologies and 
producing an educated work force to fuel the economic engine, was renamed the Donald Bren School of Information and Computer Sciences at UC Irvine.
In conjunction with the June 9, 2004, naming announcement, the school also broke ground on its new 
home, Donald Bren Hall; the first classes were held there in January 2007.
Competitively positioned among other top-ranked schools, this gift allows the 
school of information and computer sciences to continue in its upward trajectory 
of recruiting and retaining the best and the brightest students, faculty and 
researchers. 
The school of information and computer sciences has long been a program that 
combines vision and determination with wisdom. Those attributes, in concert with 
Mr. Bren’s support, will continue to allow its students, alumni and faculty to 
impact the world.
Elevated to school status in December 2002, the nationally acclaimed school 
is the first independent computer science school within the UC system and one of 
the fastest-growing programs of its kind in the nation.

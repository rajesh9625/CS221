Vision for the gift




 
The vision for the Donald Bren School of Information and Computer
                Sciences, established by Dean Debra J. Richardson in 2002 as
            part of an overall strategic initiative to advance the school’s
                global reputation, is both focused and far-reaching in its aim
                to create a top ten computer science program within five years.               
The $20 million gift will accelerate initiatives aimed at advancing
            the school's vision, further reinforcing the impact of its academic
            and research approach within the information technology discipline. 
The gift by Donald Bren provides a transformational opportunity
            to anticipate and respond to the ever-growing demands of a technology-centric
            world. 
The vision for investing the unprecedented endowment builds
            upon Mr. Bren’s long belief that quality education begins and
            ends with quality educators.
As some of the most honored faculty at UC Irvine, Bren School professors
            are trailblazers recognized with countless award and accolades in
            information and computer science education and research. 
Building
            upon that heritage, the  Bren School is now uniquely positioned to realize its vision through
            the accelerated addition of ten highly distinguished scholars alongside
            its annual recruitment of shining, young stars. 
The hallmarks of a Bren School  education are evident in the unparalleled
            UC Irvine culture, which emphasize cross-campus collaboration in
            learning and research; integrated, interdisciplinary coursework;
            innovation of cutting-edge technology and utilization of technology-enhanced
            learning. 
Our alumni successes are proof that we are forging the
            best path for tomorrow's students. For generations to come,
            Bren School students will be the beneficiaries of Mr. Bren’s selfless
            support and the school’s strategic planning.







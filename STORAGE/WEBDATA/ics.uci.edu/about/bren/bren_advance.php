Advancing the school




 
This historic gift will have a transformational impact on students,
               faculty, alumni and community friends of the Bren School. 
The school, strengthened with
            new, unprecedented resources by this gift, is aggressively moving
            toward
                its overarching goal of creating a top ten computer science program
               within five years. 
But moving forward in this new era requires
                partnership with the community that we serve. We are looking
            to engage you as a champion in the Bren School's future through the support
               of our key priorities:
»Acceleration of scholarship and fellowship awards
                to attract the most promising students»Funding endowed chairs for high-profile, mid-career faculty
                to compliment the existing endowment of ten distinguished scholars»Comprehensive expansion of industrial collaborations to transition
                emerging university technologies»Capital support for equipping and enhancing our research and
                educational spaces with cutting-edge technologies
We believe that this is the right place, right time, right plan - all
          we need is you.







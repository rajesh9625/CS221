  Donald Bren Hall Dedication.  June 20, 2007.  (L to R) Vice Chancellor Tom Mitchell, Brenda Drake, Chancellor Michael V. Drake, Donald Bren, and Dean Debra J. Richardson

Dear Bren School Community,
  2006-07 was an exciting academic year at UC Irvine’s Donald Bren School of
  Information and Computer Sciences.  
We had many successes and firsts as we
  moved into the new Donald Bren Hall and welcomed many new graduate and 
  undergraduate students. 
Today, we are an academic community of more than 1,500 students, over 100 full-time faculty and staff, and approximately 7,000 alumni worldwide.  
In 
  teaching and scholarship, we continue to be among the top in information and computer sciences.  
To add to our list of accolades, our Networked Systems 
  program was just rated number one by Academic Analytics, in addition to being ranked third in Information Systems. 
 At the Bren School, we have a unique perspective on computing and 
  information technology, stimulating society daily.  
Our vibrant community, comprised of researchers and educators as well as industry-leading scholars, 
  explores innovative topics ranging from building complete computer systems on 
  chips smaller than a finger nail to developing user interface systems that allow 
  engineers on opposite sides of the world to collaborate effectively.
 I invite you stay in touch with us throughout the year by subscribing to our RSS 
  feed or visiting our Web site regularly.
 Many thanks for your continued support of our vision.

Debra J. Richardson
  
  The Ted and Janice Smith Family Foundation Dean 

	  
	  




Directions from John Wayne Airport




From John Wayne Airport:
          
exit the airport at MacArthur - turn right on MacArthur,
turn left on Campus Dr.,
turn right on E. Peltason Dr. to purchase a parking pass at the kiosk,
go past one traffic light and watch for Los Trancos (two stop
            signs),
at the first entrance past Los Trancos turn right into Parking
            Lot 12B

From the parking lot, take paths that go away from East Peltason
            Drive, and look for Donald Bren Hall. 
The
            Bren School Front Desk is in room 6210 on the 6th floor of Donald Bren Hall. The office telephone number is (949) 824-7427
            in case you need to call.







Directions from LAX




From LAX, go east on Century Blvd; turn right on La Cienega Blvd.
            and immediately get into the left lane; watch for the sign that says "405" and
            turn left into the on ramp for the 405 Freeway going South; the drive
          is forty five minutes to an hour and a half, in traffic.

From the 405 Fwy:          
take the Jamboree exit,
turn right on Jamboree,
turn left on Campus Dr.,
turn right on E. Peltason Dr. to purchase a parking pass at
            the kiosk,
go past one traffic light and watch for Los Trancos (two stop signs),
at the first entrance past Los Trancos turn right into Parking Lot
            12B 
 

From the parking lot, take paths that go away from East Peltason
            Drive, and look for Donald Bren Hall. 
The
            Bren School Front Desk is in room 6210 on the 6th floor of Donald Bren Hall. The office telephone number is (949) 824-7427
            in case you need to call.







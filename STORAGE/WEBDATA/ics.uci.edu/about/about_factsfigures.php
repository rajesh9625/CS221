BREN SCHOOL FACTS
Only computing-focused school in UC systemFounded in 1968 as Department of Information and Computer ScienceEstablished in 2002 as an independent school, the Donald Bren School of Information and Computer Sciences (ICS)
RANKINGS
US News & World ReportBren School rated 29th among computer science graduate programs and 14th among public university programs (2014)Microsoft Academic Search website ranking of authors and organizations by citations (as of March 11, 2014)UC Irvine 20th among U.S. universities in computer science. Some top subareas:- Human-computer interaction: 4th- Software engineering: 9th- Databases: 8th
BY THE NUMBERS
2,831 students78 Faculty (full-time and lecturers)48 Staff8,000+ AlumniThree departments: Computer Science, Informatics, Statistics

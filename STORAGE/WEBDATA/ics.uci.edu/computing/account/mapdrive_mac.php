Mapping Home/Group Directory - Mac





*** Important ***
If you are using the UCInet Mobile Access (UCI wireless) or connecting from off-campus (including on-campus housing),
 please make sure that you are connected via the UCI VPN first.

 
Quick instructionsInstructions with picturesCommon errors
 

Quick Instructions
From the Finder, click Go, 
            then select Connect to Server.In the Server Address, enter one of the following and click Connect.
Home directory: smb://tron.ics.uci.edu/your_username
If you are running Mavericks, please enter: smb://samba.ics.uci.edu/your_username
Group directory: smb://samba.ics.uci.edu/group_acct_name
For Name, enter uci-ics\yourusername then enter your ICS password and click Connect.Your account is now mapped.If you would like to save the mapped drive as a shortcut, drag the newly mapped drive over to DEVICES on the left.When you map the drive next time, it will automatically show up under DEVICES.

 

Instructions with pictures
From the Finder, click on Go, then select Connect to Server.

In the Server Address, enter one of the following and click Connect.
Home directory: smb://tron.ics.uci.edu/your_username
If you are running Mavericks, please enter: smb://samba.ics.uci.edu/your_username
Group directory: smb://samba.ics.uci.edu/group_acct_name

For Name, enter uci-ics\yourusername then enter your ICS password and click Connect.

Your account is now mapped.

If you would like to save to save the mapped drive as a shortcut, drag the drive over to 
            DEVICES on the left.

When you map the drive next time, it will automatically show up 
              under DEVICES

 



Group Account Email via Thunderbird




Please make sure that you are already part of both the group account 
and the group. 
Select Tools from the menu (use the Alt key to display it in case it is hidden), select Account Settings...Under Server Settings, click on the Advanced... button to
 the bottom rightIn the space Public (shared), enter: 
"Shared/groupname/"Please note that the quotation marks (") are required.  Replace groupname with your specific group name. Do not forget the / at the end.

Click OKRestart Thunderbird







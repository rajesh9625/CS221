File Restore Request




Purpose:
Accidentally deleted files or files that are discovered to be important
 after
deletion can be recovered.
          
 Performed by:Vipada Deleon, Support Group          
  Procedure: 
  If your home directory is on the server named TRON, you can retrieve 
the data
    yourself from a snapshot.
Otherwise, send an email message to 
 helpdesk@ics.uci.edu  containing
          the
following information:
          
 Your ICS login name             A full path from your ICS home directory to the missing
 files/directories.             A complete list of what needs to be restored (if 
directories,
              there is no need to list directory contents).             The last date the files existed on or were valid on.   
       
 Lead time:
  Allow 2 working days for a file restore request.          







SGE 8.1.6 (Son of Grid Engine)
SGE is provided to schedule and manage compute jobs on the the ICS clusters. SGE 8.1.6 (Son of Grid Engine)has replaced Sun Grid Engine 6.2u5.   Version 8.1.6 provides improved shadow master support for high availability.   Information regarding grid computing at ICS is available here: http://www.ics.uci.edu/computing/linux/sge.php  More information on the Sun of Grid Engine project is available here: https://arc.liv.ac.uk/trac/SGE  All users have been migrated off of the older version and are using 8.1.6. Version 6.2u5 will be turned off permamently at the end of Fall 2014.

Drupal 7 had serious vulnerability
Please be aware that a very serious vulnerability came to light recently regarding Drupal 7.  If you run Drupal 7 you should restore your site from the 15th of October immediately.
Drupal warns unpatched users: Assume your site was hacked.  "Attackers may have copied all data out of your site and could use it maliciously. There may be no trace of the attack." "Simply updating to Drupal 7.32 will not remove backdoors."
Please see Drupals' initial announcment at https://www.drupal.org/SA-CORE-2014-005 and follow up here:  https://www.drupal.org/PSA-2014-003.  The full story is available at http://www.computerworld.com/article/2841320


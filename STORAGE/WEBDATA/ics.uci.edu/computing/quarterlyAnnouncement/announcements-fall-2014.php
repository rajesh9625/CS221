Copyright Infringement Policy
It is illegal to distribute copyrighted materials, programs, movies, music, and other digital media, without consent of the copyright holder.   If a complaint of copyright infringement is reported involving a computer or IP address assigned to you, your account will be locked until we have a chance to investigate the allegations. If copyrighted materials are found, your adviser (or Graduate Associate Dean) will be notified and you will be counseled regarding the issue. If a second complaint is filed you will be referred to the Dean of Students and the incident will be recorded in your permanent file. A third complaint will result in a recommendation that you be removed from the program.  Additional information regarding campus copyright policy can be found here: http://www.oit.uci.edu/policy/copyright/

Email Delivery to @ics.uci.edu
ICS faculty, staff and graduate students that have not already specified an alternate email delivery point are encouraged to do so now. ICS Computing Support will eliminate local email delivery and IMAP access to new email. Mail sent to your @ics.uci.edu will be resent to the alternate email delivery point. Spam and virus scanning, space management and client access to email will be provided by your alternative email provider. ICS Computing Support will continue to provide you access to old email via the school’s IMAP servers.   Computing Support recommends that you use one of the campus provided alternate email services and is available to help with the transition.  ICS provides ICS Google Apps, UCI provides several options here: http://www.oit.uci.edu/email-calendar.  .  Follow the directions at: http://www.ics.uci.edu/computing/email/delivery_point.php to set your new email delivery point. If you will be using a 3rd party provider, choose“other” and specify the email address where your ICS e-mail will be delivered.

ICS Mailman lists
ICS Computing Support provides faculty or staff sponsored mailman based mailing lists related to education, research and administrative functions.   New ad hoc mail lists can be created via OIT's mailman service. More information on the OIT mail lists is available here: http://www.oit.uci.edu/mailing-lists/mailman/

Password Reset via Security questions
Automated password recovery for ICS accounts is available at https://support.ics.uci.edu/ltb (available on campus or via vpn only).  You may now use recovery email addresses and security questions when recovering a forgotten password. For new ICS accounts the information will be collected during the account creation process. If you have an existing account you can setup recovery email address and a security question here: https://support.ics.uci.edu/ltb  After the above values have been recorded for your account,  you will be able to change the password without coming to our office.  You will be able to change your password once you provide the response to your security question. You can also request a password reset link sent to your recovery email address.  From that link you will be able to change your password.


What is the ICS Helpdesk?
The Helpdesk is a service of the ICS Computing Support Group. The purpose of the Helpdesk is to provide a single point of contact and record for technical support requests from ICS faculty, staff, and students.
 
Why a Helpdesk?
To improve the communications between the people who need services and
    the people who provide services.
  To organize service requests to ensure prompt and complete 
service.
  To simplify service request history through the database 
underneath RT.
  To increase services by allowing other groups within the 
School to use
    RT.

 

How does the ICS Helpdesk work?
The ICS Helpdesk uses a program called Request Tracker (RT).  The RT system assigns
a unique number to each ticket, which helps keep track of each help request.  Critical to the success 
of
the Helpdesk, every request must be input to RT to be assigned a ticket 
number,
whether the request originates as an e-mail message, phone message,
or walk-in visit.  A general outline of the process follows.

 E-MAIL MESSAGE
      
E-mail messages sent to helpdesk@ics.uci.edu are automatically input 
to RT
          and assigned a ticket number (#3 for purposes of this 
explanation).
        The Helpdesk analyst on duty will triage the ticket and
 assign it
          to the appropriate technician.
        Once the ticket is assigned to a technician, the sender
 of the original
          request will be notified of the ticket number by e-mail.
        Adding new information to the ticket is as simple as 
sending another
          e-mail to helpdesk@ics.uci.edu with this contained in the 
subject line
          of the message: [ics.uci.edu #3] 
        Please include the square brackets [ ] or simply reply to the original message leaving the subject line intact.
        If the technician cannot resolve the request within one
 working day,
          the requestor will be notified of the estimated time of 
completion
          and an brief explanation of the delay.
        Once the request is resolved, the ticket will be closed
 and the requestor
          will be notified by e-mail.
        If the ticket is not resolved to the requestor's 
satisfaction, the
          ticket can re-opened by responding to the closed ticket 
message and
          the ticket, with its original number, will be re-entered into 
RTs 'open'
          queue.
      
 TELEPHONE AND WALK IN
      
Helpdesk analysts will be responsible for creating tickets within
          RT for all telephoned and walk-in requests.
        Once the ticket is created by the Helpdesk analyst, the
 process follows
          the same path as outlined above.
      

 
What about e-mail messages sent to support@ics.uci.edu?
Messages sent to support@ics.uci.edu are still monitored.  If 
appropriate,
messages to 'support' will be forwarded to Helpdesk and assigned a 
ticket
number.
 

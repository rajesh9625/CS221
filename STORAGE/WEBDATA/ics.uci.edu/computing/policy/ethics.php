Introduction
As a student you are probably aware of certain ethical 
responsibilities
            you have, such as honesty in doing classwork.  Another area 
in which
            you have important ethical responsibilities is in your use 
of computing
            resources.  This document describes some of these 
responsibilities
            and explains Bren School of ICS policy on student use of 
computing resources.
            Some of these policies might be different from what you 
would expect,
            so please read over and understand this document.  As a 
student, you are
            responsible for being aware of these policies and abiding by
 them.
The ICS department provides for you, the student, a wide 
range
            of computing resources from X-Terminals to PC's to large 
multiuser
            UNIX systems.  These machines are expensive to buy and 
expensive to
            maintain, but it is our goal to provide you with the very 
best computing
            environment that we can.  Many users depend on these 
computers for
            doing class assignments, research, and for communications.  
We are
            a community of computer users, and like any community we can
 all
            make the best use of our resources if we establish some 
guidelines
            for how we can use them responsibly.
Some computing facilities, such as those which hold 
classified
            data, may establish expensive and complex security systems. 
 In our
            department we do not do this; we have some security 
mechanisms which
            greatly decrease the risk that one user will accidentally 
interfere
            with another, but it does not require great ingenuity to get
 around
            these mechanisms.  As a result, we need to trust the people 
who use
            our machines.
The fundamental principle behind our policies is this: Do 
No Harm.
            While using the computers, you should never do anything that
 harms
            another user or prevents him or her from getting work done.
If you have any questions about these rules, or if you 
suspect
            that an account (yours or someone else's) has been broken 
into, please
            contact the ICS Computing Support Group. To do this, send an email message to
            the address helpdesk@ics.uci.edu,
            or go to Support's office, CS 346 and explain the problem.
 
Computer Accounts
The Bren School of ICS has a wide range of computers 
available, located
            in several different labs.  Some of these labs are open only
 to people
            enrolled in certain classes while some are available for general 
drop-in
            use.  Each ICS student is given both a Windows account and a 
UNIX account
            to use on the lab workstations.  Different courses will require 
different
            platforms to be used.  Non ICS-majors will be given accounts 
only if required
            for an ICS course in which they are enrolled.
All ICS computers are to be used only by ICS students, 
faculty,
            and staff.  People outside ICS who wish to use computers 
should see the friendly people in Office of Information Technology. They
            provide computing services for non-ICS affiliated students. You can find a list of computer labs available to all UCI students here.
Any computer account created for you remains the property 
of the
            Regents of the University of California.  You are 
responsible for
            this account, and you may not allow any other person to use 
it.  The
            primary purpose of your account is to allow you to carry out
 your
            computing assignments and other instructional activities.  
You may
            also make modest use of these resources for other purposes, 
such
            as sending electronic mail to friends on campus, reading the
 electronic
            bulletin boards, and playing games, provided that this usage
 does
            not significantly interfere with instructional use of the 
machines.
An example of how one might "significantly interfere" would
            be to tie up a computer for game-playing when no other 
computers
            are free and someone else is waiting to use the computer to 
do an
            assignment.  If you have a game or other program you would 
like to
            make available to other users, please give it to the system 
administrator
            for public installation. You may contact the system 
administrator
            via email.
            You may not use the machines for commercial purposes, such 
as preparing
            bills for your company or advertising products, or for work 
related
            to non-UCI organizations, such as an off-campus political or
 religious
            groups.  More details about this are given below. If you are
 in doubt
            about whether some use of the machines is allowed, ask helpdesk.
 
Ethical Behavior
Here is a list of some examples of activities that the 
department
            does not allow.  If a student makes such unethical use of 
ICS computers,
            he or she will be subjected to the penalties described in the Disciplinary
            Procedures section.
You may not introduce viruses, worms, Trojan horses, 
password
              cracking, keystroke logging, or login spoofing programs on 
any University computer
              or network.  In fact, because of the serious damage such 
programs
              can cause, the Bren School of ICS faculty have adopted a 
policy which
              forbids students even to have these types of programs in 
their
              accounts or to place them onto any ICS computer.  
You may
              not store such a program on a departmental computer even 
if you
              only wish to study it.You may not try to use equipment or accounts that you 
are not permitted to use.You may not interfere with others' ability to make use 
of the
              resources.  For example, it might be reasonable to lock a 
workstation
              if you need to leave the room for two or three minutes, 
but it is not
              reasonable to lock it while you leave to buy lunch.  
Another example
              would be logging on to a significant fraction of the 
available machines at once,
              thus preventing others from fair use of the lab machines.You may not destroy other people's work either in 
electronic or physical form.You may not "spy" on people, that is, you may not 
attempt to
              gain information from their accounts or from their 
external drives when there
              is good reason to believe that they do not wish you to 
obtain that information.
              This includes both attempting to violate the protection 
facilities provided
              by the system and also taking deliberate advantage of 
someone else's
              failure to protect sensitive information on their account.
  This works
              both ways; faculty, staff, and members of Computing Support
 also have the
              responsibility to respect the privacy of the student.  For
 example, it would
              be unethical for a faculty member to browse through
              your personal messages just out of curiosity, even if they
 have a security
              level that allows them to do so.  We do, however retain 
the right to inspect
              material on your account when this is necessary to 
investigate a suspected
              violation of university rules, such as a cheating incident
 or a violation
              of the rules in this document.You may not send mail that appears to come from someone 
else.You may not advertise any commercial products or use 
your account to earn
              money.
 
Disciplinary Procedures
What happens if you violate any of these rules?  It depends on 
the seriousness of
		    the offense, but it could be one or more of the following.  
Disciplinary procedures and
		    sanctions will be consistent with those outlined in the UCI 
Implementation of "Interim
		    Policies and Procedures Applying to Campus Activities, 
Organizations, and Students, Part A.''
You may be required to meet with the chair of the Computing 
Resources Committee (CRC),
		    	the Dean of the Bren School, or the manager of the Bren School 
Support Group to discuss
		    	abuse of computing resources.Your account may be locked. (Again, we recognize an 
obligation to respect your rights
		    	as well. No student account will be locked without discussion and
 approval of the Dean of
		    	the Bren School, or the chair of the CRC, except in the case of 
security violations. It
		    	would not be ethical for us to lock your account capriciously; 
for example, we agree not
		    	to lock it simply because you send a message to a board 
expressing disagreement with some
		    	Department policy or action).For minor infractions, some form of departmental services 
(e.g., cleaning a lab) may
		    	be requested in exchange for unlocking the account.For offenses involving abusing computing resources, cheating 
on course-related work, or
		    	preventing others from working on assignments, your grade may be 
lowered in the class or you
		    	may receive a failing grade.For severe offenses, or repeating minor offenses, you may 
lose access to all Bren School
		    	computing facilities for a period of time.  Access to computing resources can be denied for a limited
		    	time (e.g., one week, the remainder of the quarter, an entire 
quarter) or permanently.You may be suspended or dismissed from the University.In serious cases, your name and a description of the 
violation may be reported to the
		    	police.  California Penal Code Section 502 makes certain computer
 abuses a crime, and the associated penalties can range up to a $10,000 fine and up to three years in
 prison.
 
Good Citizenship
Your cooperation in the following areas will help us make 
efficient use of the computing
		     resources and will avoid unnecessary impositions on the time of 
faculty, staff, and other
		     students. These are not the sort of things which we can expect to
 enforce rigidly; rather,
		     we are asking your cooperation for the benefit of the whole 
departmental community.  Violations
		     of these guidelines would not ordinarily result in any of the 
penalties listed above beyond
		     number one, unless they were especially flagrant or persist after
 faculty or staff have
		     asked you to stop.
Please be careful not to use the computer to annoy people, 
for example by sending
		     	them messages which they do not wish to receive. (The mail 
system makes it rather easy
		     	to send a message to a very large group of people; please be 
responsible in your use
		     	of this capability. In particular, when you reply to a message 
sent to a large group,
		     	avoid cc'ing your reply to the entire group unless it is a 
matter of interest to them.)Please be careful not to annoy students in the lab for any 
reason.  The lab is not a
		     	library, but we ask that each student do his/her part to help to
 maintain a pleasant working
		     	environment for all.  Activities that are not conducive to a 
pleasant work environment include,
		     	but are not limited to:  listening to music at a volume that is 
distracting to others, carrying
		     	on loud or inappropriate conversations, excessive or distracting
 cell phone use, failure to
		     	leave your workstation clean and ready for the next student, 
etc.
		     	Please do not waste anything (i.e., paper, disk space, 
CPU time, personnel time, etc.).
		     	Please throw away all trash and put your old printouts in the 
recycling bins.
 

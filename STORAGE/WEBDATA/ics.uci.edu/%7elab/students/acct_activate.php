    Account Activation »
     
    
      
        All students with a major under the Bren School of Information and
          Computer Sciences can get an ICS account. Non-majors are allowed accounts
          only if enrolled in a course under the Bren School of ICS (ICS, CS,
          CSE, Statistics, or Informatics course).
        
        Take your UCI Photo Student ID Card and see the Lab Assistant in the
          ICS computer Lab (CS 364). Ask the Lab Assistant to verify your identity
          by handing them your student ID card so that you can obtain an ICS instructional
          account.
        
           * If you do not have an ID card, you may present other form
            of picture ID if you know your ID number.
           ** If you are enrolled through Extension please go to CS 346L
            and ask to see the Lab Manager regarding your account. You will need
            to show proof of enrollment, and some form of picture ID (such as
            a driver's license).
        
        After your card has been idverified by the lab assistant, you will
          be led to one of the Windows machine used for account activation. The
          lab assistant will login to the machine and start up an session for
          you.
        You will be asked to type in your Student ID number. Type in your
            number without any punctuation characters and press Enter.You will then have to enter your name. Type your name as it appears
            on your ID card, in the form Lastname,Firstname
            Middlename  and press Enter.A summary of your account status will appear.A document on the Ethical Use of Computing will be displayed. It
            is important that you actually read the document since it contains
            information that you are required to know. Remember that at all times
            you are responsible and accountable for knowing what constitutes ethical
            behavior when using ICS computer systems.You will be asked if you have read and understood the document.
            Press I agree to continue.
            If you do not agree with the policies or have anyquestions, please
            see the Lab Manager in CS346L.
        Once you finished, you will be asked to supply an initial password
          for your account. Please remember this password. 
        Do Not use your student ID number, your name, your birthday, your
            girl/boy-friend's name etc. Do not use any part of your name or username (ie. if your name is
            Sam, you cannot have S and A next to each other in the password).Do not use * in your passwordPasswords must be at least 6 characters longPasswords must contain characters from at least 3 of the following
            4 categories:
            English Upper Case Letters A, B, C, ..., ZEnglish Lower Case Letters a, b, c, ..., zWesternized Arabic Numerals 0, 1, 2, ..., 9Non-alphanumeric characters . , ; : & % ! #
          
        If all goes well, your account will be created within 2 hours. Be sure
          to write down your login name so you don't forget it.
      
        * If your username does not match your UCInetID, please let us
          know and we can make the change manually.
      
      

    
  



	
							
		
				
					
						
						
			
			
			

						

				
			
				
			
			
			
			
						
				
				
			

				
			
		

        Group Account Request

          Each team can request a group account, where all members of the team
            can have access and store data. Please send an email to helpdesk@ics.uci.edu.
            The email should include:
          The proffessor's nameTeam numberICS username of every members of the team
          The quota for a group account is 40MB. If you need more space, please
            have your proffessor send us a request.

        Apache/MySql/Subversion

          If you need to use any of these services, please send an email to
            helpdesk@ics.uci.edu along with the group account name that you requested
            previously.

	  
    
  



	
							
		
				
					
						
						
			
			
			

						

				
			
				
			
			
			
			
						
				
				
			

				
			
		

    Project ICS Computing Policy »
    
     



        Purpose:
        The purpose of this document is to communicate ICS policy for computers
          provided to the students enrolled in Informatics 191. The policy also
          contains specific details as to the level of service provided to students
          of the course. This document also details the responsibilities of students
          using ICS provided computing resources.
        Informatics 191:
        Project in System Design, covers the following
          areas of study: Specification, design, construction, testing, and documentation
          of a complete software system. This course has special emphasis on the
          need for and use of teamwork, careful planning, and other techniques
          in working with large systems.
        Facilities:
        Informatics 191: Students in this course are provided use of ICS computing
          resources in CS 193. There are 24PCs and each team can request one system 
          be assigned to them.  No food or drinks are allowed in the lab at any time.
        Computing Resources:
        Computing resources for the class include the following:
         Windows 7 Computer Workstation Administrator rights on the machine UNIX group (gsu) account Ethernet connection to the ICS campus network ICS Standard Computing Environment
        NOTE: Printers are available at the OIT and ICS Labs. Students
          may send their print jobs to these locations.
        ICS Support Policy:
        ICS Computing Support Staff will support the computing resources made
          available to the students of Project ICS courses under the policy set
          forth in this section.
         Computer System Support between 8AM – 5PM during business
            hours. ICS does not take responsibility for any data placed by a student
            on a machine. No data backup is performed on the computers. ICS staff will not install additional software. Computers that become corrupted or unusable will be remedied by
            reinstalling the standard operating system environment. ICS will reformat the computer hard drives at the end of each 
            academic year.
        Students Responsibility:
        This section details the responsibilities of the students enrolled
          in Project ICS.
         Students must backup their own data to their home directory, group
            account directory, or portable medium such as a USB key. Students are responsible to install software on the computer they
            are assigned. Students are to have all data they wish to keep, removed by 5:00pm
            on the Friday of finals week of the quarter. Students are still required to know and adhere to the UCI computer
            and network use policy, and the ICS Ethical Use of Computing rules.
        Additional Software:
        Any software not provided by ICS with the computer is the responsibility
          of the student and instructor to requisition, purchase and install.
          These guidelines are the responsibility of the instructor or customer
          and should be presented by the instructor to the students at the beginning
          of the quarter.


	  
    
  



	
							
		
				
					
						
						
			
			
			

						

				
			
				
			
			
			
			
						
				
				
			

				
			
		

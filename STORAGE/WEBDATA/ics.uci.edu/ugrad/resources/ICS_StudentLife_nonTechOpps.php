Undergraduate Student Resources




Non-Tech Internships and Jobs
 
UCI Administrative Intern Program: Under the guidance of carefully chosen UCI mentors, Administrative
Interns work 10 hours per week in campus departments where they assume
responsibility for projects and enhance their leadership skills. Students
selected for this year-long program also enroll in a three-quarter course
through the Paul Merage School of Business where they receive 12 units of Pass/No
Pass credit for the academic year.
UCI SAGE: SAGE Scholars is a competitive program that seeks out the most talented
students at UC Irvine, targetting particularly students who demonstrate great
financial need, and provides them with training and coursework that help
develop and refine their socio-professional skills and their abilities to be
successful in the work world.
UCI California Teach Initiative: UCI Cal Teach is a unique academic program that recruits talented
undergraduate science and math majors to prepare for a teaching career. It is
jointly sponsored by the School of Biological Sciences, School of Physical
Sciences, and School of Education.
UCI Washington Academic Internship Program: The UC Irvine Washington DC Academic Internship Program, located in the
exciting environment of Washington DC, enables UCI students from ALL majors to
pursue internships, seminar and elective courses, and research in fall, winter,
or spring quarters.
UC/DC Internship Program: UCDC is an internship program in Washington, D.C., sponsored by UCI's
Career Center. It is an ideal vantage point for students to examine behind-
the-scenes activities that shape and implement our nation's future course.
UCI Sacramento Internship Program: This program is designed to immerse students into the workings of state
government while showing the important role that policy makers play in
decisions that will affect all of California.







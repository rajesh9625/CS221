Beyond Graduation





» If You're Considering Graduate School 
Speak with academic counselors, faculty mentors, and/or current graduate students about your long term academic and career goals. Consider these questions:

Why do you want to go to pursue an advanced degree?Do you have the financial resources? Do you want to apply for financial aid?Do you have the qualities necessary to succeed in graduate school?Is an advanced degree necessary to achieve your career goals?

If you are certain graduate school is your next step:

Research universities and graduate programs to find the ones most suited to your goals and interests.The UCI Career Center offers a range of services for those considering graduate school, including:
resource materials for graduate school researchtips for preparation and applicationan on-line Letter of Recommendation service
Once you have narrowed down your choices, be sure to contact those programs directly for information. Every school is different! If possible, try to speak with staff, faculty, and even current students in those programs to learn more about what graduate life is like there.You may make an appointment to speak with an ICS graduate counselor for additional advice and guidance.  Call 949-824-5156 to set up an appointment.

Watch this helpful video where ICS faculty talk about the grad school application process, and ICS grad students share their experiences https://www.youtube.com/watch?v=1IY8c4w5D6E 

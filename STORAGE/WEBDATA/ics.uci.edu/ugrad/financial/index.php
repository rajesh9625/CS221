Paying for school




We recognize that UCI is a great place — we also know that school 
tuition can be rather expensive.
              The UC Irvine Financial Aid and Scholarships office 
provides an 
online financial aid estimator with a personalized estimate of the 
cost of attendance and the estimated financial you may be eligible to 
receive.
              The Financial Aid and Scholarships office also provides
 an estimated
 breakdown of tuition and costs for attending UC Irvine. 
              To help you and your family, we offer a wide variety of
 financial aid options, including grants, loans, scholarships, paid 
research positions and student employment. 
              Please explore your options below: 
              
                              Bren 
School  Scholarships
              The school offers more than $10,000 through competitive 
scholarships. The application period for these awards usually begins in 
the spring quarter and are made possible through the generous support of
 our community and industry friends. 
              Hispanic 
College Fund Scholars Program 
Provides a one-year partial tutiton scholarship of up to one-half a 
student's tutiton and fees, not to exceed $10,000 per academic year. 
Student must be a U.S. citizen.  U. S. citizens from underrepresented 
groups (Women, African American, Hispanic American, Native American, and
 persons with disabilities) are encouraged to apply. Must be pursuing a 
degree in a physical science, technology, engineering or mathematics. more »
              ics.jobs
              An employment job board provided by the Bren School to its
 students and alumni to assist them in their search for part-time, 
fulltime and internship positions within local technology companies. more »
              UCI 
Financial Aid Office
Provides students and their families with the financial resources 
necessary to assure access to their academic goals. Includes a database 
of nationwide scholarships and grants. more »
              SAGE
 Scholars Program
Through SAGE, companies offer paid multi-year internships to talented 
and highly motivated UCI students related to their field of study. 
Through these internships, business sponsors provide invaluable 
real-world business experience and training to students, as well as 
strong mentoring. more »
              UC LEADS
              UC Irvine's UC LEADS (University of California Leadership 
Excellence through Advanced Degrees) program offers sophomore students 
an opportunity to begin their research training at the very beginning of
 their junior year. UC LEADS is designed to identify educationally or 
economically disadvantaged undergraduates pursuing courses of study in 
the sciences, engineering, and mathematics. more »
              SURF-IT
                
              The Summer Undergraduate Research Fellowship in 
Information Technology (SURF-IT) through Calit2 at UCI, provides a 
unique 10-week paid summer research opportunity for UCI undergraduates 
to become immersed in IT-related research and applications. more »
              SURF
                The
              Summer Undergraduate Research Fellowship Program (SURF) is
 a unique  offering for undergraduate diversity students with 
outstanding academic potential. The program provides students who plan 
to pursue a Ph.D. degree and enter academic careers with the tools 
needed to facilitate the application, admission, and enrollment process 
for graduate school. more »
              IM-SURE
              The Integrated Micro/Nano Summer Undergraduate Research 
Experience (IM-SURE) at UCI, provides a unique 10-week paid summer 
research opportunity for undergraduates to become fully immersed in 
cutting-edge micro/nano research and applications. more »
              SURP
              The Summer Undergraduate Research Program (SURP) provides 
funding for UCI undergraduates from all disciplines who are conducting 
summer research projects or creative activities under the guidance of 
UCI faculty members. The program offers students the opportunity to 
become immersed in a research topic for a paid full-time ten-week 
period, or the equivalent of 400 hours. more »






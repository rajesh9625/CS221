Undergraduate Student Policies




Readmission and Academic Probation
The Bren School may approve a student's time-limited
withdrawal if it becomes evident that doing so will prevent further damage to
the student's academic record. Conditions for readmission in this case are discussed at the
time of withdrawal; any academic contract conditions in place at the time of
withdrawal remain in effect upon the student's return.
The UCI readmission application must be accompanied by:
1. A written statement no longer than 2 pages that details
the following: 

reason(s) for leaving, reason(s) for returning (including new or renewed
interest/focus on Bren School major), ways that previous or existing obstacles to academic
progress have been addressed and will be addressed in the future.

2. An official transcript showing completion of at least 1
full-time quarter or semester of coursework in Computer Science or math with a GPA satisfying the conditions specified in the student's
academic contract. At least 1 course must be a transferable Computer Science course. 
 
 
 
 
 






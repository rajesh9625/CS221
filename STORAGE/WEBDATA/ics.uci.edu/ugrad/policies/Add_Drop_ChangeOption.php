» Submitting Enrollment Exception Requests
Requests
to add or drop are reviewed by the academic advising office of the school
offering the course, as well as the school of the student's major (if different). Requests
to change grade option or variable units require the approval of the academic
advising office of the student's major.
Enrollment exceptions for adds, drops, and change of grade option or variable units in ICS are not guaranteed. Exception requests will only be approved for extenuating and documented circumstances outside of the student's control. It is therefore important to continue attending class meetings and keep up with the assignments until your request is fully processed.
Requests will be denied if submitted for the following reasons:

You did notknow the deadlinesYou did not understand the Add/Drop processYou did not know the grade requirements for your degreeThe course is not required to meet Major, School, or University requirementsYou are doing poorly in the course because of difficulties with the course materialYou are doing poorly in the course because of a heavy course/work loadYou are doing poorly in the course because of failure to attend
If your drop request is approved, please
let your professor know as a courtesy once you have formally dropped the course.
 
» Course Conflicts
Time conflicts between courses may be approved on a case-by-case basis. Note that a lecture to lecture conflict will not be approved under any circumstances. If a course's lab or discussion time conflicts with another course's lecture, lab, or discussion, AND there are open spaces in both conflicting sections, follow these steps:


Enroll in one of the conflicting courses (if the conflict involves a lecture, be sure to enroll in the lecture).Obtain written approval from both instructors to allow the conflict.Forward that approval to ucounsel@uci.edu. Be sure to provide the 5-digit course codes for the conflicting courses.Allow ICS Student Affairs 2-3 business days to process the request. You will be notified by email when it has been reviewed.If approved, use WebReg to enroll in the conflicting section.







Undergraduate Student Policies




Readmission after Disqualification
Readmission is possible for students who have been academically disqualified! It is highly recommended that a student meet with an academic counselor to prepare for readmission application and requirements.  Student should visit the ICS acadmic counselor as soon as he/she has been notified of formal disqualification.
In general, a student will need to demonstrate that he/she
can finish remaining coursework within the scope of university policies
regarding credit hour unit limits.
A student readmitted after academic disqualification will be
held to the conditions of a two quarter contract specifying a course plan, unit
maximum per quarter, GPA expectations, and mandatory advising sessions. Failure to meet the contract conditions for any of two
quarters may lead to automatic disqualification.
The UCI readmission application must be accompanied by:
1. A written statement no longer than 2 pages that details
the following: 

reason(s) for
leaving, reason(s) for
returning (including new or renewed interest/focus on Bren School major), ways that previous
or existing obstacles to academic progress have been addressed and will be
addressed in the future.

2. An official transcript showing completion of a minimum of
one full-time academic year of courses taken through UCI Extension (ACCESS), a
community college or another comparable institution.  Please note that the full time course load for the quarters / semesters must be taken consecutively; breaks in academic study between a quarter or semester are not allowed.

At least six of
these courses must meet major requirements (includes math courses).Must earn Cs or higher in each course.Minimum of 2.5
cumulative GPA for this year of work. If there is
considerable evidence that a student is fully prepared to return to UCI full
time, the requirement that a full year of coursework be completed elsewhere may
be waived.  Student should speak to an academic counselor for more information.

3. If the student intends to change into or out of the Bren School upon
readmission, the submission of a Change of Major application is also required.






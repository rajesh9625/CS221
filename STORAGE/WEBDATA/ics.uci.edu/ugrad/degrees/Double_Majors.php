Double Majoring




» Steps for adding another majorREVIEW the degree requirements for the major you want to add.DRAFT a four- or five-year study plan of the courses you will need to complete for both degrees. Expect this plan to evolve as class offerings will change from year to year! Even with changes, you will still have a better idea of what classes you will be taking than if you had just taken them without any forethought. Note that a study plan is mandatory if you are: over 90 units (not including units acquired outside of UCI), a transfer student, OR entering your third quarter.MEET with a Peer Academic Advisor or academic counselor to discuss how to most efficiently meet the additional degree requirements— Don't forget to bring your study plan! If you are going to double major in two different Schools, it is advised you meet with a counselor in each School.COMPLETE the Change of Major criteria for the major you want to addSUBMIT the application through Student Access (under Applications > Change of Major)
 
Also keep in mind:
some ICS major combinations are not allowed; check the Double Major Restrictions Chart to see what degree programs are eligible for double majoring.ICS students may not add a double major during his or her senior year at UCI. Students will be directed to consider a Second Baccalaureate program in Computer Science, CSE, Informatics or Software Engineering.some majors (e.g., Biology) require that you graduate within four years regardless of single or double majors. If you are hoping to double major with degrees from another School and ICS, you may wish to do an ICS Second Baccalaureate instead.






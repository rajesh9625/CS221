» Second Baccalaureate in Software Engineering
The following minimum requirements must be complete prior to applying for a SE Second Baccalaureate:
A GPA of at least 2.5 for the first bachelor's degree.Completion of 2 courses from ICS 21-22, ICS 31, ICS 32, ICS 33, ICS 45J, INF 43; completion
 of 2 additional courses from the list of ICS 6B or Stats 67.Satisfactory completion of the lower-division writing requirement.A combined GPA of at least 2.5 and no grade lower than a "C" on all courses taken above.
*Please note that AP exam scores do not count toward the above criteria for admission.

Informatics 43: Concepts, methods, and current practice of software
 engineering. Large-scale software production, software life cycle 
models, principles and techniques for each stage of development. 
Laboratory project applying these concepts.

ICS 6B: Relations and their properties; Boolean algebras, formal languages; finite automata.

 
 
 






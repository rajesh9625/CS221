Honors opportunities




At the Bren School, highly ambitious, talented undergraduate students succeed in preparing themselves for complex 
            and exciting careers. 
Education extends from 
			    the classroom to the laboratory, studio, and library; undergraduate students have amazing opportunities to 
			    join faculty in research and have their work recognized 
			    in symposia and in print before graduating with a bachelor's degree. 
All of this is available at the Bren School, along 
			    with honors programs and special honors-level advising 
			    to help students make the most of their education

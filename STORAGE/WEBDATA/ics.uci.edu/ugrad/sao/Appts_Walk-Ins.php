Appointments and Walk-In Advising




Academic advising is available during regular office hours through appointments scheduled in advance, or same day walk-in meetings when counseling staff is available. A comparsion of the two types of meetings, as well as a timeline of when they are available, is below.

AppointmentsWalk-InsAppointments are with an Academic CounselorCan be with an Academic Counselor or a Peer Academic CounselorScheduled at least 24 hours in advance in person or by calling the ICS Student Affairs front desk at (949) 824-5156Students sign in and are seen on the same dayGenerally lasts 30 minutesGenerally 15 minutes or shorterRecommended for complicated concerns regarding probation, withdrawal, readmission, etc.Ideal for schedule planning, degree checks, questions about policies, petitions, etc.May require a waiting period of a week or more as appointment slots can fill far in advanceMeetings are available on short notice (wait times will vary)
 
 
 
 
 
 
 
 
 
 
 
 

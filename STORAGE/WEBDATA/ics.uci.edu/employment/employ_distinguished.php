Endowed Bren Chairs





The Donald Bren School of Information and Computer
Sciences was endowed with a transformational gift that included ten “Bren Chairs”
to be filled with scholars who are internationally-recognized as leaders in
emerging issues of any area of information and computer sciences, including
cross-disciplinary research integrating information and computer sciences with
other disciplines. The Bren Professors are some of the most distinguished
appointments at UC Irvine. 

Candidates should bring an integrative outlook to the discipline, enthusiasm in engaging
with professional and business communities and the general public,
collaborating with UCI scholars who study issues of information and computing
technology, and support for the development of innovative technologies and
applications. We envision Bren chair-holders to serve as catalysts on campus to
establish educational and research programs that foster an interdisciplinary
perspective. Accordingly, candidates should not only
have a strong disciplinary background with a distinguished record of scholarly
publications and extramural funding, but also a proven track record of
innovation, collaboration, stimulation and leadership in both education and
research.

Appointments will be in the Department of Computer
Science, Informatics, or Statistics at the rank of senior, distinguished
professor. Scholars doing truly cross-disciplinary research may be jointly appointed
with another school at UC Irvine. Four chairs are currently filled. 

The Bren School of ICS has excellent faculty,
innovative programs, high quality students and outstanding graduates as well as
strong relationships with local and national high tech industry. 

As one of eleven academic units at UC Irvine. an
independent school with three departments – Computer Science, Informatics, and
Statistics – the Bren School has a unique perspective that provides a broad
foundation from which to build initiatives that explore the full extent of the computing
and information disciplines. 

With 68 regular-rank faculty members, seven
full-time lecturers, approximately 250 doctoral, 150 masters, and 1000
undergraduate students, ICS is one of the largest computing programs in the
country. Many faculty in the school engage in interdisciplinary research
through various organizations such as the California Institute for
Telecommunications and Information Technology (Calit2), the Institute for
Genomics and Bioinformatics (IGB), Institute for Software Research (ISR), to
name but a few. 

Outstanding candidates in all relevant areas and at
other ranks are encouraged to contact the Dean 
(icsdean@ics.uci.edu) 

UC Irvine 
(http://www.uci.edu)  is one of the
youngest UC campuses, yet consistently
ranks among the nation’s best public universities. UCI is located three miles
from the ocean in southern California with an excellent year-round
Mediterranean climate. The area surrounding campus offers numerous outdoor and
fine arts opportunities and the public school system in Irvine is ranked one of
the highest in the nation.
 
UCI is an equal
opportunity employer committed to excellence through diversity and strongly
encourages applications from all qualified candidates, including women and
minorities. UCI is responsive to the needs of dual career couples, is dedicated
to work-life balance through an array of family-friendly policies, and is the
recipient of a National Science Foundation ADVANCE award for gender equity.






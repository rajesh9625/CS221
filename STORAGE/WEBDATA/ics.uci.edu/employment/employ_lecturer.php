     Donald Bren School of Information and Computer Sciences  Department of Informatics  Part-Time Lecturer Position 
The Department of Informatics in the Donald Bren School of Information and Computer Sciences has the occasional need for part-time lecturers to assist us in delivering a diverse curriculum.
Applicants must submit a curriculum vitae, statement of teaching, and names and contact information of three references. All materials must be uploaded at:  https://recruit.ap.uci.edu/apply/JPF03005
Salaries are commensurate with technical and teaching experience and/or level of education. Appointments may be made at varying percentages of time.
Applications for Fall Quarter positions are due on or before August 31, 2015.  Applications for Winter Quarter positions are due on or before December 2, 2015.  Applications for Spring Quarter positions are due on or before March 1, 2016.
UCI is an equal opportunity employer committed to excellence through diversity.

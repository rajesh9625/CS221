Paying for Graduate School




The Bren School has been able to support Ph.D. students admitted into the program in the past. 
A breakdown of 
tuition and costs for attending UC Irvine, can be viewed at the 
Office of the Registrar web site. 
While the School does not guarantee support for 100% of our students in the future, we continue to search for funding sources for all Ph.D. graduate students. 
M.S. students may have the opportunity to work with local companies through part-time internships and apply for teaching assistant positions.
International students should not expect to be funded unless they are officially notified in writing. 
The University of California, Irvine requires international students to pass an English Proficiency Exam before they can be appointed as a Teaching Assistant (TA).
If an international student is offered a teaching asistantship and has not taken one of these exams, they should plan to do so as soon as possible. For further information regarding international students, please visit the International Center website.

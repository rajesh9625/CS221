Graduate Student Policies: Topic and Final Defense




Topic Defense 

Once a dissertation topic is determined, it must presented it in a defense 
to your doctoral committee. Each area differs as to when students present this 
defense, so please confirm with your advisor as to when this requirement should be completed. Note that you may combine your Ph.D. advancement with your topic defense with the consent of your advisor.
A “Topic Defense ” form must be brought to the committee at the time of the defense, to be signed after they have approved your defense. These forms are 
available for download from the ICS Graduate Student Affairs website page for Forms. The signed form should be returned 
to the ICS Graduate Office. 
 
Final Defense 
The
Ph.D. final defense committee is a three member committee of senate faculty
drawn from your advancement committee. The majority of the committee MUST be
from your department. Consult with the ICS graduate counselors if changes to
the composition of your defense committee occur.
For the final defense of your dissertation, e-mail the ICS Graduate 
Office with the date, time and location of the defense, the names of your committee 
members, and the title and abstract of your dissertation. This information will 
be made available to all Bren School faculty and graduate students. In 
addition to your committee members, anyone else who wishes to attend your 
defense may do so. 

Also, be sure to obtain a "Ph.D. Form II: Report on Final Examination for the 
Ph.D. Degree" from Graduate Division, available for download here. At the end of the 
defense, have each of your committee members sign it. 

When you have finished your dissertation, submit it and the Ph.D. Form II to 
the UCI Library Archives (Langson Library, Room 525). Alternatively, the dissertation may be submitted electronically. Guidelines for thesis submission can be found on the UCI Library website here. A Thesis Submission Checklist can also be downloaded from the Graduate Division website here.
The Library will notify the UCI Graduate Division that your dissertation has 
been turned in and your degree is ready for conferral. Graduate Division will 
notify you once your degree has been conferred. 







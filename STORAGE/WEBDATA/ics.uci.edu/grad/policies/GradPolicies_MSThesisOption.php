Graduate Student Policies: M.S. Thesis Option




Master’s Thesis Option

The thesis option is available to M.S. students who do not plan to take
the comprehensive exam and who are in good academic standing with the School
(note this option not available to students in the Statistics program).
Students pursuing this option must enroll in at least two quarters of Thesis
Supervision (CS 298 or Inf 298), which will substitute for two required courses
as specified under the concentration area or track of choice.
A
committee of three faculty members, the majority of whom must be from the
student’s department, will guide the student and give final approval of the
thesis. The committee consists of an advisor, who must be from the student’s
department and who is willing to supervise the thesis project, and two other
faculty members who are willing to serve on the committee as readers. An oral
presentation of the thesis to the committee is required.M.S. Thesis Option Policy
A thesis option is available to M.S. students who do not plan to take the 
comprehensive exam and who are in good academic standing with the School. 
Students pursuing this option must enroll in at least two quarters of Thesis 
Supervision (CS 298 or INF 298) which will substitute for two required courses 
as specified under the concentration area or specialization of choice (not available to students in the Statistics program). 
A committee of three faculty members will guide the student and give final 
approval of the thesis. The committee consists of an advisor (from the student's 
department/program), who is willing to supervise the thesis project, and two 
other faculty members (one of whom must be from the student's 
department/program), who are willing to serve on the committee as readers. An 
oral presentation of the thesis to the committee is required.






Graduate Student Policies: Copyright Infringement




Bren School Copyright Infringement Policy 

It is illegal to distribute copyrighted materials—such as software, 
movies, music, pictures—without proper authorization. 

If a complaint of copyright infringement is reported involving a computer 
assigned to a Bren School student, the student's account will be locked out 
until Bren School support personnel have a chance to investigate the 
allegations. If copyrighted materials are found, they will be removed 
immediately. 

For the initial complaint (first violation), the Bren School Student Affairs 
Office (SAO) staff and the appropriate Associate Dean (as well as the academic 
advisor, for grad students) will be notified. 

The student will be required to meet in person with appropriate SAO staff to 
review applicable campus policies, and to sign a statement verifying receipt and 
notice of such policies. This signed statement will be placed in the student's 
file. 

For any subsequent complaints, the appropriate Associate Dean, academic 
advisor (for grad students) and SAO staff will be notified. 

The student will then be referred to the campus Director of Student Judicial 
Affairs for academic suspension (upon second violation) or expulsion from the 
university(upon third violation).

			The above statement is based upon:
			  IRVINE CAMPUS POLICIES , APPENDIX K: COMPUTER USE POLICY 
			  Computer Use Policy (Reference to Section 102.05), which includes "Violating the terms of applicable software licensing agreements or copyright laws."






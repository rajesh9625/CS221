Graduate Student Policies: Residency Requirements




California Residency

To establish California residency for tuition/fee purposes students
must:
File a Petition for Residence Classification with the Registrar’s
OfficeBe a U.S. citizen or permanent residentBe physically present in California for more than one calendar yearHave come to California with the intent to make California their
permanent homeShow that they intend California to become their home
Examples of (#5) include:
Registration as a voter in CaliforniaDesignation of California as their permanent residence on all
University documentsObtaining a California Driver’s License or ID CardRegistration of their car with the State of CaliforniaPayment of California income taxes as a residentMaintenance of a home in California

More information about residence classification can be found on the Registrar's website here.
 
 






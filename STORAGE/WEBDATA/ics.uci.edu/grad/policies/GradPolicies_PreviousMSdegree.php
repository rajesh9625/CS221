Graduate Student Policies: Previously Earned M.S. Degree




Bren
School Ph.D. students with an M.S. degree in computer science or a related
field from another university may qualify to receive credit for some or all
required courses.
Ph.D. students waived from the M.S. portion of their degree may not, in
future, request that they be granted an M.S. degree in Computer Science or a
closely related field from UC Irvine.
Be aware that advisors can require that students take additional
courses when appropriate.

Course equivalency will be determined by the ICS Associate Dean for
Student Affairs following a written recommendation from the student’s advisor.
Advisors may require that students take additional courses when appropriate. Final approval will be determined by the Dean of Graduate Division. Students
will be notified of the decision by email.
 
 
 
 
 






Graduate Student Policies: Committee Membership




Complete policy and procedural guidelines regarding advancement and doctoral committee
membership can be found on the instructions attached to the Ph.D Advancement to
Candidacy form.

The Advancement Committee
The advancement committee is comprised of five members: one chair, three
general members and one outside member.
Chair: must hold either a primary or joint appointment in the student’s
department, and must be a voting member of the UC Academic Senate. Senate
faculty have the following titles: Assistant Professor, Associate Professor,
Professor, and Lecturer/Senior Lecturer with Security of Employment (SOE).
General Member: at least two of the general members must hold a primary
or joint appointment in the student’s department. Non-voting Senate members or
faculty holding professorial titles at other Universities will be considered on
an exception-only basis.
Outside Member: represents the faculty at large and must be from the
Irvine Division and may not hold either a primary or joint appointment in the
student’s department. 

For students whose committee chair or advisor may have a financial interest in
their work, an Oversight Member is also required.
The Doctoral Committee
The three members of the dissertation committee is selected during the
advancement phase. It is comprised of three voting members of the University of
California Academic Senate (not necessarily the Irvine Division) or equivalent.
A majority of the committee must be affiliated with the student’s program.
The Chair of the Doctoral Committee is the faculty responsible for providing primary guidance for the student’s
dissertation. He or she must hold either a primary or joint appointment in the
student’s department, and must be a voting member of the UC Academic Senate.
For
students whose committee chair or advisor may have a financial interest in
their work, an Oversight Member is also required in addition to the two general
members.






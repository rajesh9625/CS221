Graduate Student Policies: Candidacy Committees




Candidacy Committee Duties and Responsibilities 
The candidacy committee is charged with determining the fitness of the 
student to proceed with the doctoral dissertation through a formal candidacy examination. This examination should evaluate both general preparedness in the discipline, 
and specific competence to pursue the proposed dissertation topic. 

In its deliberation, the committee ordinarily will review the student's 
academic record, preliminary examinations, evaluations by other faculty, and may 
conduct any other examination it thinks appropriate. The candidacy committee will also review an outline of the proposed 
dissertation project, and will determine by oral examination the student's 
competence in that area. When, by unanimous vote, the committee decides the student is qualified for 
the dissertation phase, it should recommend advancement to candidacy to the 
Graduate council via the Graduate Dean. 

Following its formal appointment, the committee is free to adopt whatever 
procedures it thinks appropriate to conduct the qualifying examination for 
candidacy, subject to the rules of the program and those specified below. Administration of the qualifying examination must conform to the policies 
established by the Graduate Council.
The student must be given adequate notice of the content, form and time of 
the examination. The committee must meet to decide upon the procedures to be 
followed, and the student must be given an opportunity to comment upon the 
selected procedures.Before voting upon its recommendation for or against candidacy, the committee 
as a whole shall meet with the student, and any member of the committee will 
have the right to pose appropriate questions to the student.If it decides to do so, the committee may conduct part of the examination on 
an individual basis, e.g. the student may meet with each in turn. However, the committee must conclude its examination when convened with the student present.Although the formal candidacy examination ordinarily is conducted in a single 
day, the committee may meet intermittently over a longer period, and may decide 
to reexamine the student on one or more topics after a specified interval.When the committee meets to conduct the oral candidacy examination, it must 
report to the Graduate Council via the Graduate Dean within 30 days.If the committee decides to reexamine the student at a later date or does not 
pass the student for any reason, this must be reported. The final vote and 
recommendation of the committee must be unanimous and unequivocal.
Upon completion of the candidacy examination, the results should be submitted 
to the UCI Graduate Division on the Report of the Ph.D. Candidacy Committee, 
Ph.D. Form I. This form must be signed by all committee members at the time the 
candidacy examination is concluded, and submitted even if the examination was 
failed. 

If the unanimous recommendation of the committee is favorable, the $90 advancement to candidacy fee must be paid by the student to the campus Cashier's 
Office. This will validate the Ph.D. Form I, after which it should be submitted 
to the UCI Graduate Division. The signed and validated Ph.D. Form I serves as 
the application for advancement to candidacy. 

The candidate and graduate counselor will be notified of formal advancement 
and the appointment of a doctoral committee. 






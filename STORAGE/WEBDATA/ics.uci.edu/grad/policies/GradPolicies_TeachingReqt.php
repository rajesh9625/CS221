Graduate Student Policies: Teaching Requirement




All Ph.D. students are required to participate in teaching activities
for two quarters. 
Summer teaching or teaching at another university may be
accepted in fulfillment of this requirement.
Students who fulfill this requirement with a teaching assistantship at
UCI are required to take the following:
an orientation course, ICS 398Aenroll in four units
of ICS 399 each quarter they serve as a TAattend the school's TA training
seminar during Welcome Week of fall quarter
For each
quarter assigned as a TA/Reader, the student will receive four units of
academic credit by signing up for ICS 399. Note: ICS 399 does not count toward
degree requirements
 
 
 
 
 
 






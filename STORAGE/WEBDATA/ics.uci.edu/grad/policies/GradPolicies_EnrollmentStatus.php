Graduate Student Policies: Enrollment Status




Filing Fee
Graduate students are eligible for one filing fee quarter during their
graduate career. During the filing fee quarter, students pay one-half the
registration fee and do not enroll in any classes.
The Filing Fee option applies only to students who have completed ALL
requirements for a M.S. or Ph.D. degree except for official submission of a
thesis or dissertation to the University Archives, or completion of the final
formal examination (e.g., the comprehensive examination).This means that prior
to the beginning of the "filing fee" quarter ALL other requirements for the
degree must have been met by the student, including advancement to candidacy
and the resolution of any NR or I grades. Consult the ICS Graduate Counselors
for other policies and limitations related to the filing fee.
International visa restrictions may preclude filing fee status for
students who are not citizens or permanent residents of the United States.
International students should verify their eligibility for filing fee status
via the International Center well in advance of need.
Students should note that there is a separate form when submitting a
summer filing fee petition. Both the Filing Fee Petition and the Summer Filing
Fee Petition can be found on the Graduate Division website here.
In-Absentia Registration
In absentia registration applies to full-time, regularly enrolled and
registered students who have an academic need to conduct coursework or research related
to their degree program outside of California.
More
information can be found on the In-Absentia Registration Form on the Graduate Division
website here.

Leave of Absence
All graduate students can petition for a leave of absence from the
program for a period of up to, but no more than, three quarters total. 
The LOA
petition is available for download from the Graduate Division website here. Students should fill out the form
well in advance to make sure it gets approved. The campus deadline is
Friday of the third week of classes. However, ICS recommends that students
submit the Leave of Absence petition by the registration deadline each quarter.
The leave must be approved by
both the ICS Graduate Office and the Dean of Graduate Division, as well as the
International Center for all international students. The Graduate
Division will notify students via email once the leave has been approved or
denied.
Part-Time Enrollment
Unless
enrolled in an approved part-time master's degree program (as described in
UCI’s General Catalogue), approval of part-time enrollment status may be granted
only for reasons of occupation, family responsibilities or health. Eligibility
for part-time study also depends on other factors, such as citizenship or residency,
and fellowships. ICS Ph.D. are typically not eligible to apply.
The full guidelines to determine part-time eligibility, as
well as instructions on how to submit the request, can be found on the Reduced
Fee Part-Time Study Program petition, which can be downloaded from the Graduate
Division website here.
The campus deadline to submit a Part-Time Study petition is noon on
Wednesday of the third week of classes. However, ICS recommends that students
submit the Part-Time petition by the registration deadline each quarter.
Summer Enrollment
Continuing graduate students generally do not need to enroll in Summer
Session (international students on CPT may be required to enroll). However,
students who will graduate during summer quarter MUST either be enrolled or on
Filing Fee (if eligible). During the summer, graduating students will generally
enroll in the minimum allowable units (two) of Individual Study or Thesis
Supervision with their advisor. Graduating students who do not have an advisor
should consult the ICS Graduate Counselors for assistance with summer
enrollment.






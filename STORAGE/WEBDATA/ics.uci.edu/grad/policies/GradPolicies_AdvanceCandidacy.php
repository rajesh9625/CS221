Graduate Student Policies: Advancing to Candidacy




Advancing to Candidacy for the M.S. Degree
Advancement to candidacy implies that a student is almost finished with his/her degree
requirements. This is not an automatic process. The students is responsible for
making sure his or her form is completed and gets filed. The Advancement to
Candidacy form should be submitted at least one quarter before the student plans on
graduating.
 
Advancing to Candidacy for the Ph.D. degree 
Prior to advance to candidacy for the Ph.D. degree, students must complete 
the following steps: 
Complete courseworkPass the Phase 
II exam at the Ph.D. level (if applicable to the student's program)Complete a research or survey paper of publishable quality (if applicable to the student's program)
The advancement exam is an oral presentation to a five-member committee of 
senate faculty. Note that you may combine your Ph.D. advancement with your topic defense with the consent of your advisor. The committee must be composed of the Committee Chair, three general members, and one outside member. The Chair of the candidacy commitee must hold either a primary or joint appointment in the student's department, and the majority of the committee must be from the student's department. The outside member must be a UC Irvine faculty member and may not be affiliated with the student's department.
Each committee member must agree on the research you would like 
to complete for your doctoral dissertation. 

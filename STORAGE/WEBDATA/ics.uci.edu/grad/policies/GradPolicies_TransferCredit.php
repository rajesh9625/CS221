Graduate Student Policies: Transfers of Academic Credit




Previously taken graduate-level courses may count toward ICS degree
requirements if they meet the following criteria:
Must have been passed with a grade of B or better.Must not have been applied toward a bachelor’s degree or M.S. degree
from another university.Cannot have been taken while the student was on a leave of absence from
the ICS program.Must be approved by the ICS Associate Dean for Student Affairs and the
Dean of Graduate Division
In addition, no more than one-fifth of the total units required for a
Bren School M.S. or Ph.D. degree may have been transferred from another
institution, from UCI Extension, or from Summer Sessions at any other UC
campus. Note that students may only waive two courses of their Bren School degree
requirements.
To obtain a petition to transfer course work from another university,
contact the ICS Graduate Counselor. Keep in mind that, for courses taken at
another university, students should petition to WAIVE the course. If the course
in question is a UCI course, they should petition to SUBSTITUTE the course.
Waive: If a course taken at another university is identical to a Bren
School course, contact the professor who usually teaches the course. They can
determine whether or not the course is equivalent. If it is, they can sign the
petition form, which must also be approved by the Associate Dean for Student
Affairs. Students must provide a syllabus of the course and their transcript
from the other university along with their petition form.
Substitute: If the course to be substituted is a UCI course, students
should speak to the professor who teaches the course, or to their faculty
advisor. If they agree with the substitution, they can sign the petition form.
Once the form is completed and signed by the professor or advisor, it
should be returned to the ICS Graduate Office. It will be forwarded to Graduate
Division for consideration, and once a decision is made, the ICS Graduate Office
will notify the student via email.






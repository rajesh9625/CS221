Graduate Student Policies: CPT and OPT




Curricular Practical Training (CPT)
International students may be eligible for CPT, a type of employment
authorization that allows students to complete an internship/training off
campus during the summer. CPT is not available after a student completes a degree program.
Consult the International Center for CPT
policies and application requirements. Students will need the approval of their
advisor on the CPT application.
 
 
Optional Practical Training (OPT)
International students may be eligible for OPT after graduation. OPT
provides the opportunity to gain employment experience in the student’s
major/field of study. 
Consult the International Center for OPT policies
and application requirements. The ICS Graduate Counselors can verify your
graduation date and sign the Graduation Confirmation/OPT Recommendation Form
that is required as part of the OPT application package.






Graduate Student Policies: UC Policy on Sexual Harassment




Effective December 14, 2004, the Office of the President issued a revised 
University of California Policy on Sexual Harassment and associated Procedures 
for Responding to Reports of Sexual Harassment.
These policies cover all members of the University community, including 
faculty and other academic personnel, staff employees, students, and non-student 
or non-employee participants in University programs. 
Revisions to the policy include an updated definition of sexual harassment, 
clarification of the University's obligation to respond promptly and effectively 
to reports of sexual harassment, provisions for training employees and educating 
the University community regarding sexual harassment, and a statement that the 
policy shall be implemented in a manner that recognizes principles of free 
speech and academic freedom. 

The Office of Equal Opportunity and Diversity has developed local policy and 
guidelines that implement the system wide procedures and address consensual 
relationships. 

These supersede the previous UCI Policy on Sexual Harassment: 

Section 
700-17: Guidelines for Sexual Harassment Complaint Resolution 

Section 700-16: 
Policy on Conflicts of Interest Created by Consensual Relationships 

The University of California Policy on Sexual Harassment can be found here. 

All documents can be accessed through the Sexual Harassment Officer website 
at www.sho.uci.edu 






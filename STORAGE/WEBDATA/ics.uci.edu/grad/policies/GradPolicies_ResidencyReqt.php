Graduate Student Policies: Residency Requirements




Residency Requirement
A minimum of six quarters for Ph.D. students or three quarters for M.S. 
students in academic residence is required prior to the award of the degree. 
Typically, a longer period of study (two to six years) is required for 
completion of all degree requirements.
After a Ph.D. student has been registered for over 21 quarters (seven years), 
and a M.S. student has been registered for over six quarters (two years), they are 
considered to be beyond normative time for completing the degree.
A letter regarding this status is sent to the student and to the department 
requesting a deadline be implemented for the student to complete all 
requirements and submit their dissertation. If this deadline is missed the 
student is subject to academic disqualification.
 
 
 
 
 






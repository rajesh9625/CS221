Contact a counselor




The primary focus of the Student Affairs Office is to assist students and faculty with University policies, procedures, and requirements related to the Bren ICS academic programs. The graduate staff coordinates the graduate admissions process, fellowships, and the yearly graduate student review. It also handles the various forms and administrative functions relating to graduate students.
Prospective students, please visit or contact the Bren School 
Graduate Office at  gcounsel@ics.uci.edu
 for specific questions regarding admission procedures, required exams, 
fellowships, and degree requirements that were not answered on the Admissions page.
              
Current students, please visit or contact the Bren School
  graduate counselors during walk in hours, which are 1 p.m. to
3:30 p.m. daily. If you cannot make these times, please send an email to 
set up an appointment. Counselors are also available via email for specific questions regarding your academic planning, course scheduling, and progress on degree requirements.
LOCATION 
          

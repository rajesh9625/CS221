Informatics is the interdisciplinary study of the design, 
application, use, and impact of information technology. It goes beyond 
technical design to focus on the relationship between information system
 design and use in real-world settings.
These investigations lead to new forms of system architecture, new 
approaches to system design and development, new means of information 
system implementation and deployment, and new models of interaction 
between technology and social, cultural, and organizational settings.
In the Bren School, Informatics is concerned with software 
architecture, software development, design and analysis, programming 
languages, ubiquitous computing, information retrieval and management, 
human-computer interaction, computer-supported cooperative work, and 
other topics that lie at the relationship between information technology
 design and use in social and organizational settings. 
Effective design requires an ability to analyze things from many 
different perspectives, including computer science, information science,
 organizational science, social science, and cognitive science. 
Relevant courses in those disciplines are therefore an integral part 
of the program and give this concentration a unique interdisciplinary 
flavor, which is imperative as the computing and information technology 
fields play such a pervasive role in our daily lives.






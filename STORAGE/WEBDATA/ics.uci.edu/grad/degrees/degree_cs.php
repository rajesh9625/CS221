The field of computer science is concerned with the design, analysis and 
implementation of computer systems as well as the use of computation as it is 
applied to virtually every field of study and use in the everyday world.
Computer systems can range in scope from tiny embedded systems to the 
internet as a whole. Research in computer science involves mathematical 
analysis, empirical experimentation and the implementation of proto-type 
systems. 
Core research areas include artificial intelligence and machine learning, 
bio-informatics, computer architecture, embedded systems, graphics and visual 
computing, databases and information management, multimedia, networked and 
distributed systems, programming languages and compilers, security and 
cryptography, design and analysis of algorithms, scientific computing, and 
ubiquitous computing.






Computer Science (CS) (M.S. and Ph.D.)

The Computer Science degree is a broad and flexible program which offers 
students opportunities for graduate study in the full spectrum of intellectual 
activity in computer science. 
CURRENT DEGREE REQUIREMENTS
Additional Information

The set of core and elective courses chosen by a student must be 
approved by the student's research advisor before advancement to candidacy. 
Faculty associated with each research area will provide suggested curricula for 
that area to guide students in their selection of courses. These curricula will 
also help Ph.D. students to prepare for their candidacy examination (see below) 
which must be taken in a specific research area.

 
Similarly, students may not take and count undergraduate courses in areas where they have already moved into graduate-level coursework. (The purpose of these undergraduate elective options are to help students to remedy undergraduate background deficiencies).
Ph.D. Candidacy Exam
The objective of the candidacy exam is to demonstrate in-depth knowledge of 
an area of computer science and readiness to carry out independent research at 
the doctoral level in that area. The student must complete all course 
requirements and the research project prior to advancing to candidacy. 
All requirements for candidacy including the candidacy exam must be completed 
by the end of the third year (or, for students with a previous MS in computer science or a related field, by the end of the second year). If the student does not pass on the first trial, 
they will be allowed until the end of the first quarter of the next year to 
advance to candidacy. 
The candidacy committee will consist of five faculty members, the majority of 
whom must be members of the student’s program, to administer the exam according 
to UCI Senate Policy. Please see the ICS Graduate Office for policies 
regarding the advancement committee membership. 
The student takes an oral exam, administered by the five-member committee, 
during which s/he is tested on knowledge relevant to the chosen area of 
specialization. Each area is defined by a set of topics and reading list, which 

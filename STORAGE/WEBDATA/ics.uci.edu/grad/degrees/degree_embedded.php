ICS Concentration in Embedded Systems (M.S. only)


The goal of this program is to prepare students for challenges in 
developing future embedded systems. These future systems will further 
integrate communications, multimedia, and advanced processors with 
complex embedded and real-time software for automotive, medical, 
telecommunications, and many other application domains. 
The goal of this program is to prepare students for challenges in 
developing future embedded systems. These future systems will further 
integrate communications, multimedia, and advanced processors with 
complex embedded and real-time software for automotive, medical, 
telecommunications, and many other application domains.
Furthermore, embedded systems are becoming parallel, deploying 
multiprocessor systems-on-a-chip and parallel application software. An 
in-depth knowledge of the underlying scientific and engineering 
principles is required to understand these advances and to productively 
contribute to development of such systems.
This program helps students master embedded system fundamentals, 
advanced computer architecture and compilers, networking, security, 
embedded, parallel and distributed software, and computer graphics in a 
sequence of courses and labs. Students also complete a large embedded 
systems project and may choose to write a Master's thesis.
